/* 
************************************************************************************
SCRIPT PER SPOSTARE UN CARATTERE DALLA POSIZIONE 51 ALLA POSIZIONE 251 DI 'LIBERA'
PER IL PARAMETRO CPA, SOLO PER RECORD CON IL 51° CARATTERE DI 'LIBERA' SIGNIFICATIVO
************************************************************************************
*/

-- RIEMPIO A DESTRA DEL CAMPO 'LIBERA' CON DEGLI SPAZI FINO A RAGGIUNGERE LA MASSIMA DIMENSIONE (254 CARATTERI)
UPDATE PARA SET LIBERA = RTRIM(LIBERA) + REPLICATE(SPACE(1), 254 - LEN(RTRIM(LIBERA))) 
WHERE CODICE LIKE 'CPA%' AND RTRIM(SUBSTRING(LIBERA,51,1)) <> ''

-- EFFETTUO LA SOSTITUZIONE
UPDATE PARA SET LIBERA = SUBSTRING(LIBERA,1,50) + ' ' + SUBSTRING(LIBERA,52,199) + SUBSTRING(LIBERA,51,1) + SUBSTRING(LIBERA,252,3)
WHERE CODICE LIKE 'CPA%' AND RTRIM(SUBSTRING(LIBERA,51,1)) <> ''

SELECT * FROM PARA WHERE CODICE LIKE 'CPA%' AND RTRIM(SUBSTRING(LIBERA,251,1)) <> '' ORDER BY CODICE

