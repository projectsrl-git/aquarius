
if exists (select * from dbo.sysobjects where id = object_id(N'[dbo].[TABPROP]') and OBJECTPROPERTY(id, N'IsUserTable') = 1)
	drop table [dbo].[TABPROP]
GO
 
CREATE TABLE [dbo].[TABPROP] (
	[id_unique]  uniqueidentifier ROWGUIDCOL  NOT NULL ,
	[NOME_TAB] [varchar] (50) COLLATE Latin1_General_CI_AS NULL ,
	[DESC_TAB] [varchar] (100) COLLATE Latin1_General_CI_AS NULL ,
	[FLG_ATTIVA] [bit] NULL ,
	[SEQUEN] [numeric] (10,0) NULL ,
	[NOME_CAMPO] [varchar] (50) COLLATE Latin1_General_CI_AS NULL ,
	[DESC_CAMPO] [varchar] (100) COLLATE Latin1_General_CI_AS NULL ,
	[NOTE] [text] NULL
) ON [PRIMARY] TEXTIMAGE_ON [PRIMARY]
GO


ALTER TABLE [dbo].[TABPROP] WITH NOCHECK ADD 
	CONSTRAINT [PK_TABPROP] PRIMARY KEY  CLUSTERED 
	(
		[id_unique]
	)  ON [PRIMARY] 
GO

ALTER TABLE [dbo].[TABPROP] WITH NOCHECK ADD 
	CONSTRAINT [DF_TABPROP_id_unique] DEFAULT (newid()) FOR [id_unique],
	CONSTRAINT [DF_TABPROP_NOME_TAB] DEFAULT (CONVERT(VARCHAR(30), CURRENT_TIMESTAMP, 21)) FOR [NOME_TAB],
	CONSTRAINT [DF_TABPROP_DESC_TAB] DEFAULT ('') FOR [DESC_TAB],
	CONSTRAINT [DF_TABPROP_FLG_ATTIVA] DEFAULT (0) FOR [FLG_ATTIVA],
	CONSTRAINT [DF_TABPROP_SEQUEN] DEFAULT (0) FOR [SEQUEN],
	CONSTRAINT [DF_TABPROP_NOME_CAMPO] DEFAULT ('') FOR [NOME_CAMPO],
	CONSTRAINT [DF_TABPROP_DESC_CAMPO] DEFAULT ('') FOR [DESC_CAMPO],
	CONSTRAINT [DF_TABPROP_NOTE] DEFAULT ('') FOR [NOTE]
GO
