 
-- VISTA V_MAG_GG

IF  EXISTS (SELECT * FROM sys.views WHERE object_id = OBJECT_ID(N'[dbo].[V_MAG_GG]'))
	DROP VIEW [dbo].[V_MAG_GG]
GO

CREATE VIEW [dbo].[V_MAG_GG] 
AS

SELECT
	GG.MAG_UNIQUE,
	GG.MAG_LEGAME,
	GG.MAG_DATAGG,
	GG.MAG_CODMAG,
	GG.MAG_ANAART,
	GG.MAG_GIACEN,
	GG.MAG_QTAIMP,
	GG.MAG_PRZCON,
	COALESCE(MV.MOV_VALUTA,'') AS MAG_VALUTA, 
	COALESCE(VV.VVA_CHANGE,1) AS MAG_CHANGE,
	CAST((
		CASE
			WHEN COALESCE(MV.MOV_VALUTA,'') = (SELECT TOP 1 AZI_VALU FROM U_AZI_AN) THEN GG.MAG_PRZCON
			WHEN COALESCE(MV.MOV_VALUTA,'') = '' THEN GG.MAG_PRZCON
			ELSE COALESCE(VV.VVA_CHANGE,1) * GG.MAG_PRZCON
		END
	) AS NUMERIC(19,6)) AS MAG_PRZVALU
FROM U_MAG_GG AS GG
OUTER APPLY (SELECT TOP 1 MOV_LEGAME, MOV_DTREGI, MOV_VALUTA, MOV_DTDOCU FROM U_MAG_MO WHERE MOV_DTREGI = GG.MAG_DATAGG AND MOV_LEGAME = GG.MAG_LEGAME ORDER BY MOV_DTREGI) AS MV 
OUTER APPLY (SELECT TOP 1 * FROM U_VVA_CH WHERE VVA_CODVAL = MV.MOV_VALUTA AND VVA_DATA <= (CASE WHEN MV.MOV_DTDOCU = '' THEN MV.MOV_DTREGI ELSE MV.MOV_DTDOCU END) ORDER BY VVA_DATA DESC) AS VV

GO

