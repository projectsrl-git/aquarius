-- VISTA DDT_E_FATTURE_SENZA_DDT

IF  EXISTS (SELECT * FROM sys.views WHERE object_id = OBJECT_ID(N'[dbo].[DDT_E_FATTURE_SENZA_DDT]'))
	DROP VIEW [dbo].[DDT_E_FATTURE_SENZA_DDT]
GO

CREATE VIEW [dbo].[DDT_E_FATTURE_SENZA_DDT]  
AS
	
	SELECT 
		'DELIVERY NOTE' AS TYPE,
		TT.ORD_NUMORD AS DOC_NO,
		TT.ORD_DATORD AS DOC_DATE,
		TT.ORD_CODCLI AS CUSTOMER_CODE,
		TT.ORD_RAGSOC AS CUSTOMER_NAME,
		COALESCE(P_STA.DESCRI,'') AS COUNTRY,
		CAST(ROUND(TT.ORD_IMPONIB * (CASE TT.ORD_CAMBIO WHEN 0 THEN COALESCE((SELECT ORD_CAMBIO FROM U_FAT_TT WHERE TAGGANCIO IN (SELECT DAGGANCIO FROM U_FAT_DD WHERE ORS_NUMDDT = TT.ORD_NUMORD AND ORS_DATDDT = TT.ORD_DATORD)),0) ELSE TT.ORD_CAMBIO END),2) AS NUMERIC(19,2)) AS TAXABLE_EUR,
		CAST(ROUND(TT.ORD_IMPOSTA * (CASE TT.ORD_CAMBIO WHEN 0 THEN COALESCE((SELECT ORD_CAMBIO FROM U_FAT_TT WHERE TAGGANCIO IN (SELECT DAGGANCIO FROM U_FAT_DD WHERE ORS_NUMDDT = TT.ORD_NUMORD AND ORS_DATDDT = TT.ORD_DATORD)),0) ELSE TT.ORD_CAMBIO END),2) AS NUMERIC(19,2)) AS TAX_EUR,
		CAST(ROUND(TT.ORD_VALORE * (CASE TT.ORD_CAMBIO WHEN 0 THEN COALESCE((SELECT ORD_CAMBIO FROM U_FAT_TT WHERE TAGGANCIO IN (SELECT DAGGANCIO FROM U_FAT_DD WHERE ORS_NUMDDT = TT.ORD_NUMORD AND ORS_DATDDT = TT.ORD_DATORD)),0) ELSE TT.ORD_CAMBIO END),2) AS NUMERIC(19,2)) AS TOTAL_EUR
	FROM U_BOL_TT AS TT
	LEFT OUTER JOIN PARA AS P_STA ON P_STA.CODICE = 'STA'+TT.ORD_CODNAZ
	LEFT OUTER JOIN PARA AS P_TOP ON P_TOP.CODICE = 'TOP'+TT.ORD_CAUS
	WHERE SUBSTRING(P_TOP.LIBERA,2,1) = 'V'

	UNION

	SELECT 
		'INVOICE' AS TYPE,
		TT.ORD_NUMORD AS DOC_NO,
		TT.ORD_DATORD AS DOC_DATE,
		TT.ORD_CODCLI AS CUSTOMER_CODE,
		TT.ORD_RAGSOC AS CUSTOMER_NAME,
		COALESCE(P_STA.DESCRI,'') AS COUNTRY,
		CAST(ROUND(TT.ORD_IMPONIB * TT.ORD_CAMBIO,2) AS NUMERIC(19,2)) AS TAXABLE_EUR,
		CAST(ROUND(TT.ORD_IMPOSTA * TT.ORD_CAMBIO,2) AS NUMERIC(19,2)) AS TAX_EUR,
		CAST(ROUND(TT.ORD_VALORE * TT.ORD_CAMBIO,2) AS NUMERIC(19,2)) AS TOTAL_EUR
	FROM U_FAT_TT AS TT
	LEFT OUTER JOIN PARA AS P_STA ON P_STA.CODICE = 'STA'+TT.ORD_CODNAZ
	LEFT OUTER JOIN PARA AS P_TOP ON P_TOP.CODICE = 'TOP'+TT.ORD_CAUS
	WHERE SUBSTRING(P_TOP.LIBERA,20,1) <> 'S' AND TAGGANCIO NOT IN (SELECT DAGGANCIO FROM U_FAT_DD WHERE ORS_NUMDDT <> '')

	UNION

	SELECT 
		'CREDIT NOTE' AS TYPE,
		TT.ORD_NUMORD AS DOC_NO,
		TT.ORD_DATORD AS DOC_DATE,
		TT.ORD_CODCLI AS CUSTOMER_CODE,
		TT.ORD_RAGSOC AS CUSTOMER_NAME,
		COALESCE(P_STA.DESCRI,'') AS COUNTRY,
		-1 * CAST(ROUND(TT.ORD_IMPONIB * TT.ORD_CAMBIO,2) AS NUMERIC(19,2)) AS TAXABLE_EUR,
		-1 * CAST(ROUND(TT.ORD_IMPOSTA * TT.ORD_CAMBIO,2) AS NUMERIC(19,2)) AS TAX_EUR,
		-1 * CAST(ROUND(TT.ORD_VALORE * TT.ORD_CAMBIO,2) AS NUMERIC(19,2)) AS TOTAL_EUR
	FROM U_FAT_TT AS TT
	LEFT OUTER JOIN PARA AS P_STA ON P_STA.CODICE = 'STA'+TT.ORD_CODNAZ
	LEFT OUTER JOIN PARA AS P_TOP ON P_TOP.CODICE = 'TOP'+TT.ORD_CAUS
	WHERE SUBSTRING(P_TOP.LIBERA,20,1) = 'S'

GO
