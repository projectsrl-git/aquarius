
-- VISTA PRODUZIONE_GREZZO_PER_SEMESTRE

IF  EXISTS (SELECT * FROM sys.views WHERE object_id = OBJECT_ID(N'[dbo].[PRODUZIONE_GREZZO_PER_SEMESTRE]'))
	DROP VIEW [dbo].[PRODUZIONE_GREZZO_PER_SEMESTRE]
GO

CREATE VIEW [dbo].[PRODUZIONE_GREZZO_PER_SEMESTRE]
AS
	SELECT
		ANNO,
		SEMESTRE,
		COD_ARTICOLO,
		SPESSO,
		LATO_1,
		LATO_2,
		SUM(MOV_QTAMOV) AS TOT_QTA,
		SUM(MOV_QTANR) AS TOT_QTANR
	FROM (
		SELECT
			SUBSTRING(MOV_DTREGI,1,4) AS ANNO,
			CASE
				WHEN SUBSTRING(MOV_DTREGI,6,2) IN ('01','02','03','04','05','06') THEN '1'
				WHEN SUBSTRING(MOV_DTREGI,6,2) IN ('07','08','09','10','11','12') THEN '2'
				ELSE ''
			END AS SEMESTRE,
			MOV_ANAART AS COD_ARTICOLO,
			MOV_SPESSO AS SPESSO,
			MOV_ALTEZZ AS LATO_1,
			MOV_LUNGHE AS LATO_2,
			MOV_QTAMOV,
			MOV_QTANR
		FROM U_MAG_MO WHERE 
			MOV_ANAART IN (SELECT SUBSTRING(CODICE,4,16) FROM PARA WHERE CODICE LIKE 'PRD%') AND
			SUBSTRING(MOV_NUMDOC,1,1) = 'P' AND
			MOV_SEGNO = '+'
		) AS MAIN
	GROUP BY ANNO,SEMESTRE,COD_ARTICOLO,SPESSO,LATO_1,LATO_2

