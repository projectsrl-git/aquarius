
-- VISTA FATTURATO_AGE_CLI_MESE

IF  EXISTS (SELECT * FROM sys.views WHERE object_id = OBJECT_ID(N'[dbo].[FATTURATO_AGE_CLI_MESE]'))
	DROP VIEW [dbo].[FATTURATO_AGE_CLI_MESE]
GO

CREATE VIEW [dbo].[FATTURATO_AGE_CLI_MESE]  
AS

SELECT
	ANNO,
	COD_AGENTE,
	DESCR_AGENTE,
	COD_CLIENTE,
	RAG_SOCIALE,
	TIPO_GN,
	COD_FAMIGLIA,
	FAMIGLIA,
	SUM(GEN) AS GEN,
	SUM(FEB) AS FEB,
	SUM(MAR) AS MAR,
	SUM(APR) AS APR,
	SUM(MAY) AS MAY,
	SUM(GIU) AS GIU,
	SUM(JUL) AS JUL,
	SUM(AUG) AS AUG,
	SUM(SEP) AS SEP,
	SUM(OCT) AS OCT,
	SUM(NOV) AS NOV,
	SUM(DIC) AS DIC,
	SUM(VALORE) AS TOTALE
FROM (
	SELECT 
		SUBSTRING(TT.ORD_DATORD,1,4) AS ANNO,
		TT.ORD_AGE AS COD_AGENTE,
		COALESCE(P_AGE.DESCRI,'') AS DESCR_AGENTE,
		TT.ORD_CODCLI AS COD_CLIENTE,
		TT.ORD_RAGSOC AS RAG_SOCIALE,
		TT.ORD_DATORD,
		CASE
			WHEN AN.ART_TIPOG <> '' THEN 'G'
			WHEN AN.ART_TIPON <> '' THEN 'N'
			ELSE ''
		END AS TIPO_GN,
		DD.ORD_CODART,
		RTRIM(AN.ART_TIPOG)+RTRIM(AN.ART_TIPON) AS COD_FAMIGLIA,
		COALESCE(P_TIG.DESCRI,'')+COALESCE(P_TNO.DESCRI,'') AS FAMIGLIA,
		CASE SUBSTRING(ORD_DATORD,6,2) WHEN '01' THEN (CASE SUBSTRING(P_TOP.LIBERA,11,1) WHEN 'S' THEN -1 ELSE 1 END) * DD.ORD_VALORE ELSE 0 END AS GEN,
		CASE SUBSTRING(ORD_DATORD,6,2) WHEN '02' THEN (CASE SUBSTRING(P_TOP.LIBERA,11,1) WHEN 'S' THEN -1 ELSE 1 END) * DD.ORD_VALORE ELSE 0 END AS FEB,
		CASE SUBSTRING(ORD_DATORD,6,2) WHEN '03' THEN (CASE SUBSTRING(P_TOP.LIBERA,11,1) WHEN 'S' THEN -1 ELSE 1 END) * DD.ORD_VALORE ELSE 0 END AS MAR,
		CASE SUBSTRING(ORD_DATORD,6,2) WHEN '04' THEN (CASE SUBSTRING(P_TOP.LIBERA,11,1) WHEN 'S' THEN -1 ELSE 1 END) * DD.ORD_VALORE ELSE 0 END AS APR,
		CASE SUBSTRING(ORD_DATORD,6,2) WHEN '05' THEN (CASE SUBSTRING(P_TOP.LIBERA,11,1) WHEN 'S' THEN -1 ELSE 1 END) * DD.ORD_VALORE ELSE 0 END AS MAY,
		CASE SUBSTRING(ORD_DATORD,6,2) WHEN '06' THEN (CASE SUBSTRING(P_TOP.LIBERA,11,1) WHEN 'S' THEN -1 ELSE 1 END) * DD.ORD_VALORE ELSE 0 END AS GIU,
		CASE SUBSTRING(ORD_DATORD,6,2) WHEN '07' THEN (CASE SUBSTRING(P_TOP.LIBERA,11,1) WHEN 'S' THEN -1 ELSE 1 END) * DD.ORD_VALORE ELSE 0 END AS JUL,
		CASE SUBSTRING(ORD_DATORD,6,2) WHEN '08' THEN (CASE SUBSTRING(P_TOP.LIBERA,11,1) WHEN 'S' THEN -1 ELSE 1 END) * DD.ORD_VALORE ELSE 0 END AS AUG,
		CASE SUBSTRING(ORD_DATORD,6,2) WHEN '09' THEN (CASE SUBSTRING(P_TOP.LIBERA,11,1) WHEN 'S' THEN -1 ELSE 1 END) * DD.ORD_VALORE ELSE 0 END AS SEP,
		CASE SUBSTRING(ORD_DATORD,6,2) WHEN '10' THEN (CASE SUBSTRING(P_TOP.LIBERA,11,1) WHEN 'S' THEN -1 ELSE 1 END) * DD.ORD_VALORE ELSE 0 END AS OCT,
		CASE SUBSTRING(ORD_DATORD,6,2) WHEN '11' THEN (CASE SUBSTRING(P_TOP.LIBERA,11,1) WHEN 'S' THEN -1 ELSE 1 END) * DD.ORD_VALORE ELSE 0 END AS NOV,
		CASE SUBSTRING(ORD_DATORD,6,2) WHEN '12' THEN (CASE SUBSTRING(P_TOP.LIBERA,11,1) WHEN 'S' THEN -1 ELSE 1 END) * DD.ORD_VALORE ELSE 0 END AS DIC,
		(CASE SUBSTRING(P_TOP.LIBERA,11,1) WHEN 'S' THEN -1 ELSE 1 END) * DD.ORD_VALORE AS VALORE
	FROM U_FAT_DD AS DD
	LEFT OUTER JOIN U_FAT_TT AS TT ON TT.TAGGANCIO = DD.DAGGANCIO
	LEFT OUTER JOIN PARA AS P_AGE ON P_AGE.CODICE = 'AGE'+TT.ORD_AGE
	LEFT OUTER JOIN U_ART_AN AS AN ON AN.ART_CODPRI = DD.ORD_CODART
	LEFT OUTER JOIN U_ART_PR AS PR ON PR.ART_CODPRI = DD.ORD_CODART
	LEFT OUTER JOIN PARA AS P_TIG ON P_TIG.CODICE = 'TIG'+AN.ART_TIPOG
	LEFT OUTER JOIN PARA AS P_TNO ON P_TNO.CODICE = 'TNO'+AN.ART_TIPON
	LEFT OUTER JOIN PARA AS P_TOP ON P_TOP.CODICE = 'TOP'+TT.ORD_CAUS
	WHERE
		PR.ART_CODMAT = '008' AND 
		LEN(DD.ORD_CODART) >= 15 AND 
		DD.ORD_DESART <> '*** COMMENTO ***'
	) AS MAIN
GROUP BY ANNO,COD_AGENTE,DESCR_AGENTE,COD_CLIENTE,RAG_SOCIALE,TIPO_GN,COD_FAMIGLIA,FAMIGLIA

