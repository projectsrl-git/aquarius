
-- VISTA SLOW_MOVING_CARTE_IMPREGNATE

IF  EXISTS (SELECT * FROM sys.views WHERE object_id = OBJECT_ID(N'[dbo].[SLOW_MOVING_CARTE_IMPREGNATE]'))
	DROP VIEW [dbo].[SLOW_MOVING_CARTE_IMPREGNATE]
GO

CREATE VIEW [dbo].[SLOW_MOVING_CARTE_IMPREGNATE]  
AS

SELECT * FROM (
	SELECT 
		MAG_CODMAG AS CODMAG,
		MAG_ANAART AS CODART,
		MAG_DESART AS DESART,
		MAG_ALTEZZ AS LATO1,
		MAG_LUNGHE AS LATO2,
		MAG_FORNIT AS CODFOR,
		MAG_DESFOR AS DESFOR,
		MAG_GIACEN AS GIACEN,
		SUBSTRING(MAG_DATAGG,9,2) + '/' + SUBSTRING(MAG_DATAGG,6,2) + '/' + SUBSTRING(MAG_DATAGG,1,4) AS DATCAR,
		DATEDIFF(DD,(CONVERT(DATETIME,MAG_DATAGG,111)),GETDATE()) AS GG_GIACENZA
	FROM U_MAG_GG
	WHERE
		MAG_CODMAG = '3' AND 
		MAG_ANAART LIKE '01.003.%' AND
		MAG_GIACEN > 0
	) AS MAIN
WHERE GG_GIACENZA > 90

