
-- VISTA ARTICOLI_MOVIMENTATI_RIC_OFF

IF  EXISTS (SELECT * FROM sys.views WHERE object_id = OBJECT_ID(N'[dbo].[ARTICOLI_MOVIMENTATI_RIC_OFF]'))
	DROP VIEW [dbo].[ARTICOLI_MOVIMENTATI_RIC_OFF]
GO

CREATE VIEW [dbo].[ARTICOLI_MOVIMENTATI_RIC_OFF]  
AS

SELECT
	CODMAG,
	ANAART,
	DESART,
	UMMAGA,
	COALESCE((SELECT SUM(MAG_GIACEN) FROM U_MAG_GG WHERE MAG_CODMAG = MAIN.CODMAG AND MAG_ANAART = MAIN.ANAART AND MAG_GIACEN > 0),0) AS GIACEN,
	ULTIMA_LOCAZIONE
FROM (
	SELECT
		MO.MOV_CODMAG AS CODMAG,
		MO.MOV_ANAART AS ANAART,
		MO.MOV_DESART AS DESART,
		PR.ART_UMMAGA AS UMMAGA,
		(SELECT TOP 1 RTRIM(MOV_FILA_X) + ' - ' + RTRIM(MOV_PIAN_X) + ' - ' + RTRIM(MOV_UBIC_X) FROM U_MAG_MO WHERE MOV_ANAART = MO.MOV_ANAART AND MOV_CODMAG = MO.MOV_CODMAG ORDER BY MOV_DTREGI+MOV_ORAMOV DESC) AS ULTIMA_LOCAZIONE
	FROM U_MAG_MO AS MO
	LEFT OUTER JOIN U_ART_PR AS PR ON PR.ART_CODPRI = MO.MOV_ANAART
	WHERE
		MO.MOV_ANAART <> '' AND
		MO.MOV_CODMAG IN ('RIC','OFF')
	) AS MAIN
GROUP BY CODMAG,ANAART,DESART,UMMAGA,ULTIMA_LOCAZIONE
