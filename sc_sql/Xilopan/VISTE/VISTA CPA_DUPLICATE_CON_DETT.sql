-- VISTA CPA_DUPLICATE_CON_DETT

IF  EXISTS (SELECT * FROM sys.views WHERE object_id = OBJECT_ID(N'[dbo].[CPA_DUPLICATE_CON_DETT]'))
	DROP VIEW [dbo].[CPA_DUPLICATE_CON_DETT]
GO

CREATE VIEW [dbo].[CPA_DUPLICATE_CON_DETT] 
AS
SELECT *,
	COALESCE((SELECT COUNT(*) AS CONTA FROM U_CLI_AN WHERE CLI_CONPAG = SUBSTRING(MAIN.CODICE,4,3)),'') AS RICORRENZE_IN_ANAGR_CLIENTI,
	COALESCE((SELECT COUNT(*) AS CONTA FROM U_FOR_AN WHERE FOR_CONPAG = SUBSTRING(MAIN.CODICE,4,3)),'') AS RICORRENZE_IN_ANAGR_FORNITORI,
	COALESCE((SELECT COUNT(*) AS CONTA FROM U_FAT_TT WHERE ORD_CPA = SUBSTRING(MAIN.CODICE,4,3)),'') AS RICORRENZE_IN_FATTURE_CLIENTI,
	COALESCE((SELECT TOP 1 ORD_DATORD FROM U_FAT_TT WHERE ORD_CPA = SUBSTRING(MAIN.CODICE,4,3) ORDER BY ORD_DATORD DESC),'') AS DATA_ULTIMA_RICORRENZA_IN_FATTURE_CLIENTI,
	COALESCE((SELECT COUNT(*) AS CONTA FROM U_ORF_TT WHERE ORD_CPA = SUBSTRING(MAIN.CODICE,4,3)),'') AS RICORRENZE_IN_ORDINI_FORNITORI,
	COALESCE((SELECT TOP 1 ORD_DATORD FROM U_ORF_TT WHERE ORD_CPA = SUBSTRING(MAIN.CODICE,4,3) ORDER BY ORD_DATORD DESC),'') AS DATA_ULTIMA_RICORRENZA_IN_ORDINI_FORNITORI,
	COALESCE((SELECT COUNT(*) AS CONTA FROM INDICE WHERE MOV_MODPAG = SUBSTRING(MAIN.CODICE,4,3)),'') AS RICORRENZE_IN_CONTABILITA_GENERALE,
	COALESCE((SELECT TOP 1 MOV_DTREG FROM INDICE WHERE MOV_MODPAG = SUBSTRING(MAIN.CODICE,4,3) ORDER BY MOV_DTREG DESC),'') AS DATA_ULTIMA_RICORRENZA_IN_CONTABILITA_GENERALE
FROM (
	SELECT 
		CAST(MAIN.SCAFISS AS VARCHAR(3))+
		CAST(MAIN.PERIMPO AS VARCHAR(6))+
		CAST(MAIN.SCAFISS_1R AS VARCHAR(3))+
		MAIN.SOLOIMPIVA+
		SUBSTRING(MAIN.LIBERA,1,50) AS CHIAVE,
		COUNT(*) AS NR_CODICI_CON_STESSA_CHIAVE,
		P2.CODICE,RTRIM(P2.DESCRI) AS DESCRI,
		P2.DISATTIVO
	FROM PARA  AS MAIN
	LEFT OUTER JOIN PARA AS P2 ON  P2.codice like 'CPA%' AND P2.LIBERA = MAIN.LIBERA AND P2.SCAFISS = MAIN.SCAFISS
						 AND P2.PERIMPO = MAIN.PERIMPO AND P2.SCAFISS_1R = MAIN.SCAFISS_1R AND P2.SOLOIMPIVA = MAIN.SOLOIMPIVA
	where MAIN.codice like 'CPA%' 
	GROUP BY MAIN.LIBERA,MAIN.SCAFISS,MAIN.PERIMPO,MAIN.SCAFISS_1R,MAIN.SOLOIMPIVA,P2.CODICE,P2.DESCRI,P2.DISATTIVO
	) AS MAIN
