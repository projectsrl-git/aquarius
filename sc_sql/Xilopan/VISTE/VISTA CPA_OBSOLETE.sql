-- VISTA CPA_OBSOLETE

IF  EXISTS (SELECT * FROM sys.views WHERE object_id = OBJECT_ID(N'[dbo].[CPA_OBSOLETE]'))
	DROP VIEW [dbo].[CPA_OBSOLETE]
GO

CREATE VIEW [dbo].[CPA_OBSOLETE] 
AS

SELECT * FROM (
	SELECT SUBSTRING(PA.CODICE,4,10) AS CODICE_CPA,PA.DESCRI AS DESCRIZIONE,
		COALESCE((SELECT COUNT(*) AS CONTA FROM U_CLI_AN WHERE CLI_CONPAG = SUBSTRING(PA.CODICE,4,3)),'') AS RICORRENZE_IN_ANAGR_CLIENTI,
		COALESCE((SELECT COUNT(*) AS CONTA FROM U_FOR_AN WHERE FOR_CONPAG = SUBSTRING(PA.CODICE,4,3)),'') AS RICORRENZE_IN_ANAGR_FORNITORI,
		COALESCE((SELECT TOP 1 ORD_DATORD FROM U_FAT_TT WHERE ORD_CPA = SUBSTRING(PA.CODICE,4,3) ORDER BY ORD_DATORD DESC),'') AS DATA_ULTIMA_RICORRENZA_IN_FATTURE_CLIENTI,
		COALESCE((SELECT TOP 1 ORD_DATORD FROM U_ORF_TT WHERE ORD_CPA = SUBSTRING(PA.CODICE,4,3) ORDER BY ORD_DATORD DESC),'') AS DATA_ULTIMA_RICORRENZA_IN_ORDINI_FORNITORI,
		COALESCE((SELECT TOP 1 MOV_DTREG FROM INDICE WHERE MOV_MODPAG = SUBSTRING(PA.CODICE,4,3) ORDER BY MOV_DTREG DESC),'') AS DATA_ULTIMA_RICORRENZA_IN_CONTABILITA_GENERALE
	FROM PARA as PA
	where PA.codice like 'CPA%' AND PA.DISATTIVO = 0
) AS MAIN
WHERE 
	DATEDIFF(DAY, cast(DBO.RIBALTA2(MAIN.DATA_ULTIMA_RICORRENZA_IN_FATTURE_CLIENTI) AS datetime), GETDATE()) > (3*365) AND 
	DATEDIFF(DAY, cast(DBO.RIBALTA2(MAIN.DATA_ULTIMA_RICORRENZA_IN_ORDINI_FORNITORI) AS datetime), GETDATE()) > (3*365) AND
	DATEDIFF(DAY, cast(DBO.RIBALTA2(MAIN.DATA_ULTIMA_RICORRENZA_IN_CONTABILITA_GENERALE) AS datetime), GETDATE()) > (3*365)
GO

SELECT * FROM CPA_OBSOLETE ORDER BY CODICE_CPA


