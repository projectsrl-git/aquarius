
-- VISTA PARTITE_FORNITORI_BLOCCATI

IF  EXISTS (SELECT * FROM sys.views WHERE object_id = OBJECT_ID(N'[dbo].[PARTITE_FORNITORI_BLOCCATI]'))
	DROP VIEW [dbo].[PARTITE_FORNITORI_BLOCCATI]
GO

CREATE VIEW [dbo].[PARTITE_FORNITORI_BLOCCATI] 
AS

	SELECT
		PAR_CODCLI AS COD_FORNITORE,
		PAR_RAGSOC AS RAG_SOCIALE,
		PAR_TIPOPA AS TIPO_PARTITA,
		DESTPA AS DESCR_TIPO_PARTITA,
		CASE PAR_BLOCCO WHEN 1 THEN 'SI' ELSE 'NO' END AS PAG_BLOCCATO,
		PAR_NPART AS NR_PARTITA,
		PAR_BLOCCO AS NR_DOCUMENTO,
		PAR_DTFAT AS DT_DOCUMENTO,
		PAR_NREG AS NR_REGISTRAZIONE,
		PAR_DTREG AS DT_REGISTRAZIONE,
		PAR_DTSCAD AS DT_SCADENZA,
		PAR_FATT AS IMPORTO
	FROM (

		SELECT 
			PART.* ,
			COALESCE(CPA.DESCRI,'')  AS DESCPA ,
			COALESCE(PARTPA.DESCRI,'')  AS DESTPA ,
			CASE WHEN COALESCE(SUBSTRING(PARTOP.LIBERA,2,1),'') IN ('S','F','A') THEN 'FAT' ELSE 'PAG' END AS FLAG_NATURA,
			COALESCE(SUBSTRING(PARTOP.LIBERA,11,1),'') AS FLAG_NOTAC_TOP ,
			CASE WHEN PAR_NPART IN (SELECT DISTINCT PAR_NPART
										FROM PART_FOR AS PART 
										LEFT OUTER JOIN PARA AS PARTOP
										ON 'TOP'+PART.PAR_TIPFAT = PARTOP.CODICE
										WHERE PAR_CODSOC='01'
											--AND PAR_CODCLI='2110100003856'
											--AND PAR_NPART IN (SELECT PAR_NPART FROM PART_FOR WHERE PAR_CODSOC='01' AND PAR_DTREG BETWEEN '<<RIBALTA2(PERDAL)>>' AND '<<RIBALTA2(PERAL)>>') 
											AND PAR_NPROG<>100 
											AND COALESCE(SUBSTRING(PARTOP.LIBERA,2,1),'') IN ('S','F','A') AND COALESCE(SUBSTRING(PARTOP.LIBERA,11,1),'') <> 'S') THEN 'OK'
			
				WHEN PAR_NPART IN (SELECT DISTINCT PAR_NPART FROM
									(SELECT PAR_NPART, COUNT(*) AS CONTA_RIGHE_PARTITA FROM PART_FOR AS PART 
										WHERE PAR_CODSOC='01' --AND PAR_CODCLI='2110100003856'
											--AND PAR_NPART IN (SELECT PAR_NPART FROM PART_FOR WHERE PAR_CODSOC='01' AND PAR_DTREG BETWEEN '<<RIBALTA2(PERDAL)>>' AND '<<RIBALTA2(PERAL)>>') 
											AND PAR_NPROG<>100 GROUP BY PAR_NPART) AS TMP_PART WHERE CONTA_RIGHE_PARTITA >= 2) THEN 'OK'
			
				ELSE 'KO'
			END AS LEGATA,
			COALESCE((SELECT TOP 1 MOV_DESMOV FROM MOV_CONT WHERE MOV_NUMPRO = PART.PAR_NUMPRO AND MOV_CONTO = PART.PAR_CODCLI),'') AS MOV_DESMOV,
			COALESCE((SELECT TOP 1 MOV_NPROT FROM MOV_CONT WHERE MOV_NUMPRO = PART.PAR_NUMPRO AND MOV_CONTO = PART.PAR_CODCLI),'') AS MOV_NPROT
		FROM PART_FOR AS PART 
		LEFT OUTER JOIN PARA AS CPA
			ON 'CPA'+PART.PAR_CODPAG = CPA.CODICE 
		LEFT OUTER JOIN PARA AS PARTOP
			ON 'TOP'+PART.PAR_TIPFAT = PARTOP.CODICE
		LEFT OUTER JOIN PARA AS PARTPA
			ON 'TPA'+PART.PAR_TIPOPA = PARTPA.CODICE
		WHERE PAR_NPROG <> 100 --AND PAR_CODCLI = '2110100003856'
		) AS PARTITARIO_FORNITORI

	WHERE FLAG_NATURA = 'FAT' AND PAR_TIPOPA <> '' AND PAR_BLOCCO = 1


