
-- VISTA GIACENZE_U12_U13

IF  EXISTS (SELECT * FROM sys.views WHERE object_id = OBJECT_ID(N'[dbo].[GIACENZE_U12_U13]'))
	DROP VIEW [dbo].[GIACENZE_U12_U13]
GO

CREATE VIEW [dbo].[GIACENZE_U12_U13]
AS
	SELECT
		MAG_CODMAG AS MAGAZZINO,
		MAG_ANAART AS ARTICOLO,
		ART_DESCR AS DESCRIZIONE,
		ART_UMMAGA AS UM,
		MAG_GIACEN AS GIACENZA
	FROM U_MAG_GG, U_ART_PR, U_ART_AN
	WHERE
	U_MAG_GG.MAG_ANAART = U_ART_PR.ART_CODPRI AND U_MAG_GG.MAG_ANAART = U_ART_AN.ART_CODPRI AND 
	(ART_CODCOL IN ('U12','U13') OR ART_CODCOLRET IN ('U12','U13'))

