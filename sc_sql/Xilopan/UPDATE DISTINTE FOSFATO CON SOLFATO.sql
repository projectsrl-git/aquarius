
-- SOSTITUIRE PER TUTTI I CODICI/DI.BA IL FOSFATO 01.004.0010 CON IL SOLFATO 01.004.0004

UPDATE U_DIS_DD SET
	DIS_CODART = '01.004.0004',
	DIS_DESCRI = (SELECT ART_DESCR FROM U_ART_PR WHERE ART_CODPRI = '01.004.0004')
WHERE DIS_CODART = '01.004.0010' AND DIS_GRUPPO IN (SELECT SUBSTRING(CODICE,4,16) FROM PARA WHERE CODICE LIKE 'PRDP_%')




