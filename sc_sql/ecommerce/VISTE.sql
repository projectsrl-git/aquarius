IF  EXISTS (SELECT * FROM sys.views WHERE object_id = OBJECT_ID(N'[dbo].[LOG_OPERATIVO]'))
DROP VIEW [dbo].[LOG_OPERATIVO]

GO

CREATE VIEW [dbo].[LOG_OPERATIVO]
AS
	SELECT 
		UTENTI.USERID,
		UTENTI.RUOLO,
		CASE UTENTI.COGNOME WHEN UTENTI.NOME THEN UTENTI.COGNOME ELSE UTENTI.NOME +' '+ UTENTI.COGNOME END AS DESCRIZIONE,
		LOG_UTENTI.DATA,
		LOG_UTENTI.ORA,
		LOG_UTENTI.OPERAZIONE AS OPERAZIONE,
		COALESCE(MENU.DESCRIZIONE,'') AS MENU  
	FROM LOG_UTENTI
	LEFT OUTER JOIN UTENTI
	ON LOG_UTENTI.OPERATORE=UTENTI.USERID 
	LEFT OUTER JOIN MENU 
	ON RTRIM(SUBSTRING(LOG_UTENTI.OPERAZIONE,1,3))=MENU.ORDINE
GO



