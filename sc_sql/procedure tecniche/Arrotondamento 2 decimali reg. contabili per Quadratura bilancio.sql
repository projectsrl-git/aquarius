
--SERVE PER I PROBLEMI DI "UN CENTESIMO" SUL BILANCIO, L'HO FATTA GIRARE IN SOPRAD E L'HA RISOLTO


SELECT ROUND(MOV_IMP,2),* FROM MOV_CONT WHERE MOV_DTREG BETWEEN '2015/01/01' AND '2015/12/31'
AND MOV_NUMPRO IN (SELECT MOV_NUMPRO FROM MOV_CONT WHERE MOV_DTREG BETWEEN '2015/01/01' AND '2015/12/31' AND MOV_CONTO='501001') AND (CONVERT(INT, MOV_IMP*100)- MOV_IMP*100) <>0

UPDATE MOV_CONT SET MOV_IMP = ROUND(MOV_IMP,2) WHERE MOV_DTREG BETWEEN '2015/01/01' AND '2015/12/31'
AND MOV_NUMPRO IN (SELECT MOV_NUMPRO FROM MOV_CONT WHERE MOV_DTREG BETWEEN '2015/01/01' AND '2015/12/31' AND MOV_CONTO='501001') AND (CONVERT(INT, MOV_IMP*100)- MOV_IMP*100) <>0
