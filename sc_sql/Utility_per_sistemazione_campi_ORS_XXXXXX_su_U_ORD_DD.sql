UPDATE U_ORD_DD SET 
	U_ORD_DD.ORS_NUMORD = U_ORD_TT.ORD_NUMORD,
	U_ORD_DD.ORS_DATORD = U_ORD_TT.ORD_DATORD,
	U_ORD_DD.ORS_CODCLI = U_ORD_TT.ORD_CODCLI,
	U_ORD_DD.ORS_RAGSOC = U_ORD_TT.ORD_RAGSOC,
	U_ORD_DD.ORS_CODEMI = U_ORD_TT.ORD_CODEMI,
	U_ORD_DD.ORS_RAGEMI = U_ORD_TT.ORD_RAGEMI
FROM U_ORD_DD JOIN U_ORD_TT
ON U_ORD_DD.DAGGANCIO = U_ORD_TT.TAGGANCIO
