in base ai valori della tabella di testata U_ORD_TT, e quindi rispettivamente ai campi:

	- U_ORD_TT.ORD_NUMORD 
	- U_ORD_TT.ORD_DATORD 
	- U_ORD_TT.ORD_CODCLI 
