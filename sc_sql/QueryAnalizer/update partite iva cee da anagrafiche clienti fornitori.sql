SELECT 
	CL.CLI_IVACEE,
	MAIN.*,
	'UPDATE MOV_CONT SET MOV_PARTITA_IVA_INTRA = ''' + RTRIM(CL.CLI_IVACEE) + ''' WHERE MOV_NUMPRO = ''' + RTRIM(MAIN.MOV_NUMPRO) + '''' AS SC_UPDATE
FROM (
	SELECT CON_TIPOCO,MOV_NUMPRO,SUBSTRING(MC.MOV_CONTO,5,10) AS COD_CLIFOR,MOV_ANNO
	FROM MOV_CONT AS MC
	LEFT OUTER JOIN CONTI AS CO ON CO.CON_CONTO = MC.MOV_CONTO
	LEFT OUTER JOIN PARA AS PARA1 ON 'TOP'+MC.MOV_TOP = PARA1.CODICE
	WHERE CON_TIPOCO IN ('C') AND MOV_ANNO = '2015' AND SUBSTRING(PARA1.LIBERA,2,1)='S'
	GROUP BY CON_TIPOCO,MOV_NUMPRO,SUBSTRING(MC.MOV_CONTO,5,10),MOV_ANNO) AS MAIN
LEFT OUTER JOIN U_CLI_AN AS CL ON CL.CLI_CODCLI = MAIN.COD_CLIFOR
WHERE CL.CLI_IVACEE <> '.' AND CL.CLI_IVACEE <> ''


-- *****************************************
-- FORNITORI
-- *****************************************
SELECT 
	FO.FOR_IVACEE,
	MAIN.*,
	'UPDATE MOV_CONT SET MOV_PARTITA_IVA_INTRA = ''' + RTRIM(FO.FOR_IVACEE) + ''' WHERE MOV_NUMPRO = ''' + RTRIM(MAIN.MOV_NUMPRO) + '''' AS SC_UPDATE
FROM (
	SELECT CON_TIPOCO,MOV_NUMPRO,SUBSTRING(MC.MOV_CONTO,5,10) AS COD_CLIFOR,MOV_ANNO
	FROM MOV_CONT AS MC
	LEFT OUTER JOIN CONTI AS CO ON CO.CON_CONTO = MC.MOV_CONTO
	LEFT OUTER JOIN PARA AS PARA1 ON 'TOP'+MC.MOV_TOP = PARA1.CODICE
	WHERE CON_TIPOCO IN ('F') AND MOV_ANNO = '2015' AND SUBSTRING(PARA1.LIBERA,2,1)='S'
	GROUP BY  CON_TIPOCO,MOV_NUMPRO,SUBSTRING(MC.MOV_CONTO,5,10),MOV_ANNO) AS MAIN
LEFT OUTER JOIN U_FOR_AN AS FO ON FO.FOR_CODCLI = MAIN.COD_CLIFOR
WHERE FO.FOR_IVACEE <> '.' AND FO.FOR_IVACEE <> ''


