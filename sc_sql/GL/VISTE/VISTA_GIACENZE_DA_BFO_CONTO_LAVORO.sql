
-- VISTA GIACENZE_DA_BFO_CONTO_LAVORO
IF  EXISTS (SELECT * FROM sys.views WHERE object_id = OBJECT_ID(N'[dbo].[GIACENZE_DA_BFO_CONTO_LAVORO]'))
	DROP VIEW [dbo].[GIACENZE_DA_BFO_CONTO_LAVORO]
GO

CREATE VIEW [dbo].[GIACENZE_DA_BFO_CONTO_LAVORO]
AS
	
	SELECT
		NR_DDT_CONTO_LAV,
		DATA_DDT_CONTO_LAV,
		CODFOR,
		COALESCE(FR.FOR_RAGSOC,'') AS RAGSOC,
		CODMAG,
		CODART,
		COALESCE(PR.ART_DESCR,'') AS DESART,
		COALESCE(PR.ART_UMMAGA,'') AS UM,
		GIACEN
	FROM (	
		SELECT
			COALESCE(MO.MOV_NUMDOC,'') AS NR_DDT_CONTO_LAV,
			COALESCE(MO.MOV_DTDOCU,'') AS DATA_DDT_CONTO_LAV,
			COALESCE(MO.MOV_FORNIT,'') AS CODFOR,
			GG.MAG_CODMAG AS CODMAG,
			GG.MAG_ANAART AS CODART,
			GG.MAG_GIACEN AS GIACEN
		FROM U_MAG_GG AS GG
		OUTER APPLY (SELECT TOP 1 * FROM U_MAG_MO WHERE MOV_TOP = '053' AND MOV_LEGAME = GG.MAG_LEGAME AND MOV_SEGNO = '+' ORDER BY MOV_DTREGI,MOV_ORAMOV) AS MO
		) AS MAIN
	LEFT OUTER JOIN U_FOR_AN AS FR ON FR.FOR_CODCLI = MAIN.CODFOR
	LEFT OUTER JOIN U_ART_PR AS PR ON PR.ART_CODPRI = MAIN.CODART
	WHERE NR_DDT_CONTO_LAV <> ''

GO

SELECT * FROM GIACENZE_DA_BFO_CONTO_LAVORO ORDER BY DATA_DDT_CONTO_LAV, NR_DDT_CONTO_LAV
