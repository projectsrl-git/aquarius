-- VISTA SCADENZE

IF  EXISTS (SELECT * FROM sys.views WHERE object_id = OBJECT_ID(N'[dbo].[VISTA_SCADENZE]'))
	DROP VIEW [dbo].[VISTA_SCADENZE]
GO

CREATE VIEW [dbo].[VISTA_SCADENZE]  
AS

	SELECT
		'C' AS CLI_FOR,
		CONVERT(VARCHAR(25), GETDATE(), 111) as DATA_CORRENTE,
		PA.PAR_DTFAT,
		PA.PAR_VALUTA,
		PA.PAR_DTSCAD,
		CAST(IIF(PAR_DTSCAD BETWEEN CAST(YEAR(GETDATE())  AS CHAR(4))+'/01/01' AND CONVERT(VARCHAR(25), GETDATE(), 111), 1, 0 ) AS BIT) AS FLAG_SCADUTO,
		PA.PAR_CODCLI,
		PA.PAR_TIPFAT,
		PA.PAR_TMOV,
		PA.PAR_FATT,
		CAST(IIF( COALESCE(SUBSTRING(PARTOP.LIBERA,2,1),'') ='S', 1, 0 ) AS BIT) AS FLAG_FAT_PAG ,
		CAST(IIF( COALESCE(SUBSTRING(PARTOP.LIBERA,11,1),'') ='S', 1, 0 ) AS BIT) AS FLAG_NOTA_CREDITO ,
		PA.PAR_CONTABILITA_PREVISIONALE
	FROM PART_CLI AS PA 
	LEFT OUTER JOIN PARA AS PAR
	ON PAR.CODICE='CPA'+PA.PAR_CODPAG 
	LEFT OUTER JOIN PARA AS PARTOP
	ON PARTOP.CODICE='TOP'+PA.PAR_TIPFAT  
	WHERE 
		PAR_NPROG <> 100 
		AND COALESCE(SUBSTRING(PARTOP.LIBERA,2,1),'') IN ('P','S')
		AND SUBSTRING(PAR_DTSCAD,1,4) = CAST(YEAR(GETDATE())  AS CHAR(4))

	UNION

	SELECT
		'F' AS CLI_FOR,
		CONVERT(VARCHAR(25), GETDATE(), 111) as DATA_CORRENTE,
		PA.PAR_DTFAT,
		PA.PAR_VALUTA,
		PA.PAR_DTSCAD,
		CAST(IIF(PAR_DTSCAD BETWEEN CAST(YEAR(GETDATE())  AS CHAR(4))+'/01/01' AND CONVERT(VARCHAR(25), GETDATE(), 111), 1, 0 ) AS BIT) AS FLAG_SCADUTO,
		PA.PAR_CODCLI,
		PA.PAR_TIPFAT,
		PA.PAR_TMOV,
		PA.PAR_FATT,
		CAST(IIF( COALESCE(SUBSTRING(PARTOP.LIBERA,2,1),'') ='S', 1, 0 ) AS BIT) AS FLAG_FAT_PAG ,
		CAST(IIF( COALESCE(SUBSTRING(PARTOP.LIBERA,11,1),'') ='S', 1, 0 ) AS BIT) AS FLAG_NOTA_CREDITO ,
		PA.PAR_CONTABILITA_PREVISIONALE
	FROM PART_FOR AS PA 
	LEFT OUTER JOIN PARA AS PAR
	ON PAR.CODICE='CPA'+PA.PAR_CODPAG 
	LEFT OUTER JOIN PARA AS PARTOP
	ON PARTOP.CODICE='TOP'+PA.PAR_TIPFAT  
	WHERE 
		PAR_NPROG <> 100 
		AND COALESCE(SUBSTRING(PARTOP.LIBERA,2,1),'') IN ('P','S')
		AND SUBSTRING(PAR_DTSCAD,1,4) = CAST(YEAR(GETDATE())  AS CHAR(4))
GO
