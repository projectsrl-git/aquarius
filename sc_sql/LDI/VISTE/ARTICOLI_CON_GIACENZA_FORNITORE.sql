
IF  EXISTS (SELECT * FROM sys.views WHERE object_id = OBJECT_ID(N'[dbo].[ARTICOLI_CON_GIACENZA_FORNITORE]'))
	DROP VIEW [dbo].[ARTICOLI_CON_GIACENZA_FORNITORE]
GO

CREATE VIEW [dbo].[ARTICOLI_CON_GIACENZA_FORNITORE]
AS

SELECT
	COD_ART,
	DES_ART,
	UM,
	COD_MAG,
	DES_MAG,
	COD_FORN,
	DES_FORN,
	SUM(GIACENZA) AS GIACENZA
FROM (
	SELECT
		PR.ART_CODPRI AS COD_ART,
		PR.ART_DESCR AS DES_ART,
		PR.ART_UMMAGA AS UM,
		COALESCE(GG.MAG_CODMAG,'') AS COD_MAG,
		COALESCE(P_MAG.DESCRI,'') AS DES_MAG,
		COALESCE(PR.ART_CODF1,'') AS COD_FORN,
		COALESCE(FO1.FOR_RAGSOC,'') AS DES_FORN,
		COALESCE(GG.MAG_GIACEN,0) AS GIACENZA
	FROM U_ART_PR AS PR
	LEFT OUTER JOIN U_ART_AN AS AN ON AN.ART_CODPRI = PR.ART_CODPRI
	LEFT OUTER JOIN U_MAG_GG AS GG ON GG.MAG_ANAART = PR.ART_CODPRI
	LEFT OUTER JOIN PARA AS P_MAG ON P_MAG.CODICE = 'MAG'+GG.MAG_CODMAG
	LEFT OUTER JOIN U_FOR_AN AS FO1 ON FO1.FOR_CODCLI = PR.ART_CODF1
	) AS MAIN
GROUP BY 
	COD_ART,
	DES_ART,
	UM,
	COD_MAG,
	DES_MAG,
	COD_FORN,
	DES_FORN

GO

SELECT * FROM ARTICOLI_CON_GIACENZA_FORNITORE ORDER BY DES_FORN,COD_ART,DES_ART,COD_MAG