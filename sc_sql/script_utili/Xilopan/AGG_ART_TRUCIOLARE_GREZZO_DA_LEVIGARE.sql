
-- SISTEMAZIONE ARTICOLI TRUCIOLARE GREZZI DA LEVIGARE PRIVI DI TIPO GREZZO (U_ART_AN.ART_TIPOG)

UPDATE U_ART_AN SET ART_TIPOG = (
	SELECT COALESCE(RTRIM(SUBSTRING(LIBERA,1,1)),'')
	FROM 
	PARA WHERE CODICE = 'PRD' + U_ART_AN.ART_CODPRI),
	ART_TIPON = ''
WHERE 
ART_TIPOG = '' AND 
ART_CODPRI IN (SELECT ART_CODPRI FROM U_ART_PR WHERE ART_CODMAT = '008' AND ART_CODCOL = '') AND 
ART_CODPRI IN (SELECT SUBSTRING(CODICE,4,16) FROM PARA WHERE SUBSTRING(CODICE,1,3) = 'PRD')
