/* ABBINAMENTO CON I TIPI PRODOTTO GREZZO 'TIG' E NOBILITATO 'TNO' */
SELECT	SUBSTRING(P1.CODICE,4,1) AS TIPO_PRODOTTO,
		P1.DESCRI AS DES_TIPO_PRODOTTO,
		CASE WHEN SUBSTRING(P1.CODICE,1,3)='TIG' THEN 'GREZZO' ELSE 'NOBILITATO' END AS G_N ,
		SUBSTRING(P2.CODICE,4,2) AS GRUPPO_LISTINO,
		P2.DESCRI AS DES_GRUPPO_LISTINO 
FROM PARA AS P1 
LEFT OUTER JOIN PARA AS P2
ON 'GLI'+P1.LIBERA = P2.CODICE
WHERE (P1.CODICE LIKE 'TIG%' OR P1.CODICE LIKE 'TNO%') AND P2.CODICE LIKE 'GLI%' 
ORDER BY P1.CODICE


