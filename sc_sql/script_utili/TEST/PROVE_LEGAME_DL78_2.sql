SELECT SUM(TOT) AS TOTALE
FROM (
		SELECT  TT.ORD_NUMORD,
				DD.ORD_VALORE AS TOT,
				TT.TAGGANCIO,
				DD.DAGGANCIO
		FROM U_FAT_TT AS TT,
			U_FAT_DD AS DD
		WHERE 
			TT.TAGGANCIO = DD.DAGGANCIO
			AND (TT.ORD_NUMORD = '000366'
			AND TT.ORD_DATORD = '2010/05/05')
) AS T1


select ivf_top,ivf_imponi,ivf_numfat,ivf_codcli,* from u_ivf_cl where ivf_anno = '2010' order by ivf_top,ivf_numfat,ivf_imponi desc



		SELECT  DISTINCT DD.ORS_NUMDDT,
						 DD.ORS_DATDDT
		FROM U_FAT_TT AS TT,
			U_FAT_DD AS DD
		WHERE 
			TT.TAGGANCIO = DD.DAGGANCIO
			AND ORS_NUMDDT != ''
			AND (TT.ORD_NUMORD = '000366'		-- NUMERO FATTURA
			AND TT.ORD_DATORD = '2010/05/05')	-- DATA FATTURA



		SELECT  DISTINCT DD.ORS_NUMORC,
						 DD.ORS_DATORC
		FROM U_BOL_TT AS TT,
			U_BOL_DD AS DD
		WHERE 
			TT.TAGGANCIO = DD.DAGGANCIO
			AND ORS_NUMORC != ''
			AND (TT.ORD_NUMORD = '000425'		-- NUMERO DDT
			AND TT.ORD_DATORD = '2010/05/03')	-- DATA DDT




		SELECT SUM(TOT) AS TOTALE
		FROM (
				SELECT  TT.ORD_NUMORD,
						DD.ORD_VALORE AS TOT,
						TT.TAGGANCIO,
						DD.DAGGANCIO
				FROM U_ORD_TT AS TT,
					U_ORD_DD AS DD
				WHERE 
					TT.TAGGANCIO = DD.DAGGANCIO
					AND (TT.ORD_NUMORD = '000549'
					AND TT.ORD_DATORD = '2010/04/23')
		) AS T1




