--- TUTTE LE RICEVUTE RIPRESENTATE
SELECT DOPPIE, RIC_NFATT,RIC_DTFATT,RIC_DTSCAD,RIC_TRANC,RIC_DTFATT,RIC_CODCLI,RIC_RAGSOC FROM (
SELECT COUNT(*) AS DOPPIE, RIC_NFATT,RIC_DTSCAD,RIC_TRANC,RIC_DTFATT,RIC_CODCLI,RIC_RAGSOC FROM (
SELECT RICT.RIC_NUMDIS,RICD.RIC_NUMPAR,RICT.RIC_NFATT,RICT.RIC_DTFATT,RICT.RIC_DTSCAD,RICT.RIC_TRANC,RICT.RIC_CODCLI,RICT.RIC_RAGSOC 
FROM (SELECT * FROM U_RIC_TT WHERE RIC_RICMAN!='S' AND RIC_CGE=1) AS RICT
LEFT OUTER JOIN U_RIC_DD AS RICD
ON RICT.TAGGANCIO=RICD.DAGGANCIO
WHERE RIC_DATDIS BETWEEN '2012/01/01' AND '2012/06/30' ) AS TEMP
GROUP BY RIC_NFATT,RIC_DTFATT,RIC_DTSCAD,RIC_TRANC,RIC_DTFATT,RIC_CODCLI,RIC_RAGSOC ) AS T1
WHERE DOPPIE>1


SELECT RICT.RIC_NUMDIS,RICD.RIC_NUMPAR,RICT.RIC_NFATT,RICT.RIC_DTFATT,RICT.RIC_DTSCAD,RICT.RIC_TRANC,RICT.RIC_CODCLI,RICT.RIC_RAGSOC,RICT.* 
FROM (SELECT * FROM U_RIC_TT WHERE RIC_RICMAN!='S' AND RIC_CGE=1) AS RICT
LEFT OUTER JOIN U_RIC_DD AS RICD
ON RICT.TAGGANCIO=RICD.DAGGANCIO
WHERE RICT.RIC_NFATT='000978' AND RICT.RIC_DTFATT='2012/03/26' AND RICT.RIC_DTSCAD='2012/04/30' AND RIC_TRANC=954.070


SELECT RICT.RIC_NUMDIS,RICD.RIC_NUMPAR,RICT.RIC_NFATT,RICT.RIC_DTFATT,RICT.RIC_DTSCAD,RICT.RIC_TRANC,RICT.RIC_CODCLI,RICT.RIC_RAGSOC,RICT.* 
FROM (SELECT * FROM U_RIC_TT WHERE RIC_RICMAN!='S' AND RIC_CGE=1) AS RICT
LEFT OUTER JOIN U_RIC_DD AS RICD
ON RICT.TAGGANCIO=RICD.DAGGANCIO
WHERE RICT.RIC_NFATT='001146' AND RICT.RIC_DTFATT='2012/04/06' AND RICT.RIC_DTSCAD='2012/07/10' AND RIC_TRANC=2276.320


SELECT * FROM PART_CLI 
WHERE PAR_NPROG<>'100' AND PAR_TIPODO='FAT' 
AND PAR_NPROT='000978' AND PAR_NPART =122357 AND PAR_FATT=954.070 AND PAR_DTSCAD='2012/04/30'




AND  RICT.RIC_NFATT='001982'



