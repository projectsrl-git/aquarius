--- ACCESSO AL PARTITARIO PER PARTITA VERIFICO IL SALDO
SELECT 
	PAR_CODCLI,
	PAR_NPART,
	SUM(DARE)-SUM(AVERE) AS SALDO
FROM (
SELECT 
	PAR_CODCLI,
	PAR_NPART,
	CASE WHEN  PAR_TMOV = 'D' THEN PAR_FATT ELSE 0 END AS DARE,
	CASE WHEN  PAR_TMOV = 'A' THEN PAR_FATT ELSE 0 END AS AVERE
FROM PART_CLI
WHERE PAR_NPROG <> '100' AND PAR_NPART=124542 ) AS T1
GROUP BY PAR_CODCLI, PAR_NPART




