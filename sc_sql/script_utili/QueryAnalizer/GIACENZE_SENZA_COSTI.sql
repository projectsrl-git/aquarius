
SELECT 
	T1.CODART,
	ART.ART_DESCR AS DESCRI,
	ART.ART_TIPART AS ART_TIPART,
	T1.UM,
	GIACENZA,
	COALESCE((SELECT TOP 1 ORD_VALORE/ORD_QTAORD 	 
				FROM U_BFO_DD 
				WHERE ORD_CODART=T1.CODART 
						AND ORS_DATORD < '2012/12/31' 
						AND ORD_QTAORD>0
						AND ORD_VALORE>0
				ORDER BY ORS_DATORD DESC ),0) AS COSTO_DA_ULTIMA_BOLLA_DI_CARICO ,
	COALESCE((SELECT AVG(CASE WHEN MOV_QTAMOV > 0 THEN MOV_VALACQ/MOV_QTAMOV ELSE 0 END) AS COSTO_MEDIO
			FROM U_MAG_MO
			WHERE MOV_DTDOCU BETWEEN '2012/01/01' AND '2012/12/31'
				AND MOV_ANAART=T1.CODART 
				AND MOV_SEGNO='+'
				AND MOV_QTAMOV<>0
				AND MOV_VALACQ<>0),0) AS COSTO_MEDIO_MAGAZZINO 
FROM (SELECT MOV_ANAART AS CODART,MOV_UM AS UM,
	SUM((CASE WHEN MOV_SEGNO = '+' THEN 1 ELSE -1 END) *  MOV_QTAMOV ) AS GIACENZA 
FROM U_MAG_MO
WHERE MOV_QTAMOV<>0
AND	RTRIM(MOV_ANAART)<>'.'
GROUP BY MOV_ANAART,MOV_UM) AS T1
LEFT OUTER JOIN U_ART_PR AS ART 
ON T1.CODART = ART.ART_CODPRI
WHERE GIACENZA <> 0 
ORDER BY CODART



