
/*########################################################## 2008 */
SELECT 	COUNT(*) AS CONTA,
	*
FROM
(
	SELECT  --CON_CONTO,
		SUBSTRING(CON_CONTO,5,LEN(CON_CONTO)) AS CLIENTE
		--,CON_TIPOCO
	FROM 	CONTI
	WHERE 	
		CON_ANNO = '2008' and
		CON_TIPOCO IN ('C','F')
) AS T1
GROUP BY 
	CLIENTE
	--,CON_CONTO
	--,CON_TIPOCO
ORDER BY 
	CONTA DESC,CLIENTE



