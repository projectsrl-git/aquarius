SELECT 
	COSTI_MEDI.CODART,
	COSTI_MEDI.UM_ACQ,
	COSTI_MEDI.COSTO AS COSTO_MEDIO ,
	COSTI_MEDI.UM_VEN,
	RICAVI_MEDI.QTA AS QTA_VENDUTA,
	RICAVI_MEDI.VALORE AS FATTURATO,
	RICAVI_MEDI.RICAVO AS RICAVO_MEDIO 
FROM ( SELECT 
	CODART,
	UM_ACQ,
	UM_VEN,
	SUM(QTA*COSTO_UNITARIO) AS COSTO
FROM (SELECT 
	CODART,
	UM_ACQ,
	UM_VEN,
	COD_COMPONENTE,
	QTA,
	CASE 
		WHEN COSTO_DA_ULTIMA_BOLLA_DI_CARICO >0 
		THEN COSTO_DA_ULTIMA_BOLLA_DI_CARICO
		ELSE 
			CASE 
				WHEN COSTO_MEDIO_DA_MOVIMENTI_DI_CARICO >0 
				THEN COSTO_MEDIO_DA_MOVIMENTI_DI_CARICO
				ELSE COSTO_COMPONENTE_DA_ANAGRAFICA_ARTICOLI
			END 
		END AS COSTO_UNITARIO,
	COSTO_DISTINTA_DA_ANAGRAFICA_ARTICOLI 
FROM ( SELECT 
	DIS_GRUPPO AS CODART,
	'KG' AS UM_ACQ,
	ARTG.ART_UMMAGA AS UM_VEN,
	DIS_CODART AS COD_COMPONENTE, 
	DIS_QTA AS QTA,
    COALESCE((SELECT TOP 1 ORD_VALORE/ORD_QTAORD 	 
				FROM U_BFO_DD 
				WHERE ORD_CODART=DIS.DIS_CODART AND ORS_DATORD BETWEEN '2012/01/01' AND '2012/12/31' 
				ORDER BY ORS_DATORD DESC ),0) AS COSTO_DA_ULTIMA_BOLLA_DI_CARICO ,
	COALESCE((SELECT SUM(MOV_VALACQ)/SUM(MOV_QTAMOV)  
			FROM U_MAG_MO 
		WHERE MOV_SEGNO='+' AND MOV_PREACQ>0
		AND MOV_DTDOCU BETWEEN '2012/01/01' AND '2012/12/31'  
		AND MOV_ANAART = DIS.DIS_CODART
		GROUP BY MOV_ANAART) ,0) AS COSTO_MEDIO_DA_MOVIMENTI_DI_CARICO ,
	COALESCE(ART.ART_PRZCO1  ,0) AS COSTO_COMPONENTE_DA_ANAGRAFICA_ARTICOLI,
	ARTG.ART_PRZCO1 AS COSTO_DISTINTA_DA_ANAGRAFICA_ARTICOLI
FROM U_DIS_DD AS DIS
LEFT OUTER JOIN U_ART_PR AS ART
ON DIS.DIS_CODART = ART.ART_CODPRI AND ART.ART_UMMAGA<>'NR'
INNER JOIN U_ART_PR AS ARTG
ON DIS.DIS_GRUPPO = ARTG.ART_CODPRI 
) AS COSTI_COMPONENTI ) AS COSTI
GROUP BY CODART,UM_ACQ,UM_VEN 
 ) AS COSTI_MEDI
LEFT OUTER JOIN (SELECT 
					ORD_CODART AS CODART, 
					SUM(ORD_QTAORD) AS QTA,
					SUM(ORD_VALORE) AS VALORE, 
					SUM(ORD_VALORE) / SUM(ORD_QTAORD) AS RICAVO 
				FROM (SELECT ORD_CODART,ORD_QTAORD,ORD_VALORE 
						FROM U_FAT_DD 
						WHERE ORD_QTAORD>0 
						AND ORS_DATORD BETWEEN '2012/01/01' AND '2012/12/31') AS RICAVI
				GROUP BY ORD_CODART ) AS RICAVI_MEDI
ON COSTI_MEDI.CODART = RICAVI_MEDI.CODART 
-- WHERE COSTI_MEDI.CODART='A 0026' 


