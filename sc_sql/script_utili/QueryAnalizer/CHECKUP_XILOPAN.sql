	ORD_DATORD,
	COUNT(*) AS QUANTI_DDT, 
	SUM(ORD_VALORE) AS AQ_DT_VALOR ,
	SUM(ORD_QTANR) AS AQ_DT_QTANR,
	SUM(ORD_QTAKG) AS AQ_DT_QTAKG,
	SUM(ORD_QTAMC) AS AQ_DT_QTAMC,
	SUM(ORD_QTAMQ) AS AQ_DT_QTAMQ,
	SUM(ORD_QTAML) AS AQ_DT_QTAML 
FROM(
	SELECT 
		ORD_NUMORD,
		ORD_DATORD,
		TT.ORD_VALORE ,
		SUM(ORD_NUMBOB) AS ORD_QTANR,
		SUM(ORD_QTAKG) AS ORD_QTAKG,
		SUM(ORD_QTAMC) AS ORD_QTAMC,
		SUM(ORD_QTAMQ) AS ORD_QTAMQ,
		SUM(ORD_QTAML) AS ORD_QTAML 
	FROM U_BOL_TT AS TT
	LEFT OUTER JOIN U_BOL_DD AS DD
	ON TT.TAGGANCIO=DD.DAGGANCIO AND ORD_CODART <>'' AND ORD_DESART<>'*** COMMENTO ***'
	GROUP BY ORD_DATORD,TAGGANCIO ,ORD_NUMORD,TT.ORD_VALORE ) AS T1
WHERE ORD_DATORD='2012/03/29'
GROUP BY ORD_DATORD
ORDER BY ORD_DATORD 


