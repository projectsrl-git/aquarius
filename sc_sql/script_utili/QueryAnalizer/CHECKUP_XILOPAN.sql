
--- ORDINI 
SELECT	
	ORD_DATORD,
	COUNT(*) AS QUANTI_ORDINI, 
	SUM(ORD_VALORE) AS AQ_OC_VALOR ,
	SUM(ORD_QTANR) AS AQ_OC_QTANR,
	SUM(ORD_QTAKG) AS AQ_OC_QTAKG,
	SUM(ORD_QTAMC) AS AQ_OC_QTAMC,
	SUM(ORD_QTAMQ) AS AQ_OC_QTAMQ,
	SUM(ORD_QTAML) AS AQ_OC_QTAML 
FROM(
	SELECT
		TT.ORD_NUMORD,
		TT.ORD_DATORD,
		TT.TAGGANCIO,
		SUM(DD.ORD_VALORE) AS ORD_VALORE,
		SUM(DD.ORD_NUMBOB) AS ORD_QTANR,
		SUM(DD.ORD_QTAKG) AS ORD_QTAKG,
		SUM(DD.ORD_QTAMC) AS ORD_QTAMC,
		SUM(DD.ORD_QTAMQ) AS ORD_QTAMQ,
		SUM(DD.ORD_QTAML) AS ORD_QTAML 
	FROM U_ORD_TT AS TT
	LEFT OUTER JOIN U_ORD_DD AS DD
	ON TT.TAGGANCIO=DD.DAGGANCIO AND DD.ORD_CODART <>'' AND DD.ORD_DESART<>'*** COMMENTO ***'
	GROUP BY TT.ORD_NUMORD, TT.ORD_DATORD, TT.TAGGANCIO) AS T1
WHERE ORD_DATORD='2012/03/29'
GROUP BY ORD_DATORD
ORDER BY ORD_DATORD 


-- DDT

