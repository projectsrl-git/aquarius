

--*** GIACENZE DI TIPO "Materie prime TUTTE (CODICE 01.* ) eccetto le carte con codice 01.003.*"
SELECT MAG_CODMAG,MAG_DATAGG,MAG_ANAART,MAG_CODART,MAG_DESART,MAG_GIACEN,MAG_QTAIMP,* FROM U_MAG_GG
WHERE MAG_ANAART like '01.%' and 
MAG_ANAART NOT LIKE '01.003.%'
ORDER BY MAG_CODMAG,MAG_CODART

DELETE FROM U_MAG_GG WHERE MAG_ANAART like '01.%' and MAG_ANAART NOT LIKE '01.003.%'

--*** MOVIMENTI DI TIPO "Materie prime TUTTE (CODICE 01.* ) eccetto le carte con codice 01.003.*"
SELECT MOV_CODMAG,MOV_DTREGI,MOV_ANAART,MOV_CODART,MOV_DESART,MOV_QTAMOV,MOV_SEGNO,* FROM U_MAG_MO
WHERE MOV_ANAART like '01.%' and MOV_ANAART  NOT LIKE '01.003.%'
ORDER BY MOV_CODMAG,MOV_CODART

DELETE FROM U_MAG_MO WHERE MOV_ANAART like '01.%' and MOV_ANAART  NOT LIKE '01.003.%'







--*** GIACENZE DI TIPO "PANNELLI TRUCIOLARI"
SELECT MAG_CODMAG,MAG_DATAGG,MAG_ANAART,MAG_CODART,MAG_DESART,MAG_GIACEN,MAG_QTAIMP,* FROM U_MAG_GG LEFT OUTER JOIN U_ART_PR ON U_MAG_GG.MAG_ANAART = U_ART_PR.ART_CODPRI
WHERE ART_CODMAT = '008'
ORDER BY MAG_CODMAG,MAG_CODART

DELETE FROM U_MAG_GG WHERE MAG_ANAART IN (SELECT ART_CODPRI FROM U_ART_PR WHERE ART_CODMAT = '008')


--*** MOVIMENTI DI TIPO "PANNELLI TRUCIOLARI"
SELECT MOV_CODMAG,MOV_DTREGI,MOV_ANAART,MOV_CODART,MOV_DESART,MOV_QTAMOV,MOV_SEGNO,* FROM U_MAG_MO LEFT OUTER JOIN U_ART_PR ON U_MAG_MO.MOV_ANAART = U_ART_PR.ART_CODPRI
WHERE ART_CODMAT = '008'
ORDER BY MOV_CODMAG,MOV_CODART

DELETE FROM U_MAG_MO WHERE MOV_ANAART IN (SELECT ART_CODPRI FROM U_ART_PR WHERE ART_CODMAT = '008')


