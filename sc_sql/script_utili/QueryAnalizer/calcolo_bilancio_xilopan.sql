		CON_CONTO,CON_DESCR,
		SUM(CON_IMP_D) AS CON_IMP_D,SUM(CON_IMP_A) AS CON_IMP_A, 
		SUM(CON_DA01) AS CON_DA01,SUM(CON_AV01) AS CON_AV01,
		SUM(CON_DA02) AS CON_DA02,SUM(CON_AV02) AS CON_AV02,
		SUM(CON_DA03) AS CON_DA03,SUM(CON_AV03) AS CON_AV03,
		SUM(CON_DA04) AS CON_DA04,SUM(CON_AV04) AS CON_AV04,
		SUM(CON_DA05) AS CON_DA05,SUM(CON_AV05) AS CON_AV05,
		SUM(CON_DA06) AS CON_DA06,SUM(CON_AV06) AS CON_AV06,
		SUM(CON_DA07) AS CON_DA07,SUM(CON_AV07) AS CON_AV07,
		SUM(CON_DA08) AS CON_DA08,SUM(CON_AV08) AS CON_AV08,
		SUM(CON_DA09) AS CON_DA09,SUM(CON_AV09) AS CON_AV09,
		SUM(CON_DA10) AS CON_DA10,SUM(CON_AV10) AS CON_AV10,
		SUM(CON_DA11) AS CON_DA11,SUM(CON_AV11) AS CON_AV11,
		SUM(CON_DA12) AS CON_DA12,SUM(CON_AV12) AS CON_AV12


		FROM (SELECT CON_CONTO,CON_DESCR,

			CASE WHEN CON_IMP_D>CON_IMP_A THEN CON_IMP_D-CON_IMP_A ELSE 0 END AS CON_IMP_D,
			CASE WHEN CON_IMP_D<CON_IMP_A THEN CON_IMP_A-CON_IMP_D ELSE 0 END AS CON_IMP_A, 
			CASE WHEN CON_DA01>CON_AV01 THEN CON_DA01-CON_AV01 ELSE 0 END AS CON_DA01,
			CASE WHEN CON_DA01<CON_AV01 THEN CON_AV01-CON_DA01 ELSE 0 END AS CON_AV01, 
			CASE WHEN CON_DA02>CON_AV02 THEN CON_DA02-CON_AV02 ELSE 0 END AS CON_DA02,
			CASE WHEN CON_DA02<CON_AV02 THEN CON_AV02-CON_DA02 ELSE 0 END AS CON_AV02, 
