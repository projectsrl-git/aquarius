/* ELABORA MOV_CONT PER OTTENERE IL SALDO A LIVELLO DI CONTO CONTABILE SOMMANDO TUTTI I DARE E TUTTI GLI AVERE*/
/* ATTIVITA' PASSIVITA' SALDO*/


SELECT 
		MOV_ANNO,MOV_CONTO,
        SUM(MOV_IMP_D) AS MOV_IMP_D,
		SUM(MOV_IMP_A) AS MOV_IMP_A,
		SUM(MOV_IMP_D-MOV_IMP_A) AS MOV_IMP_SALDO 
		FROM (SELECT MOV_ANNO,MOV_CONTO,
				CASE WHEN MOV_TMOV='D' THEN MOV_IMP ELSE 0 END AS MOV_IMP_D,
				CASE WHEN MOV_TMOV='A' THEN MOV_IMP ELSE 0 END AS MOV_IMP_A
				FROM MOV_CONT
				WHERE LEN(MOV_CONTO)>5
				AND MOV_CONTO>='01' AND MOV_CONTO<'30'
				AND MOV_SOC='01'
		AND MOV_ANNO='2012') AS T1

GROUP BY MOV_ANNO,MOV_CONTO
ORDER BY MOV_CONTO




 
