			CASE WHEN CON_DA03<CON_AV03 THEN CON_AV03-CON_DA03 ELSE 0 END AS CON_AV03, 
			CASE WHEN CON_DA04>CON_AV04 THEN CON_DA04-CON_AV04 ELSE 0 END AS CON_DA04,
			CASE WHEN CON_DA04<CON_AV04 THEN CON_AV04-CON_DA04 ELSE 0 END AS CON_AV04, 
			CASE WHEN CON_DA05>CON_AV05 THEN CON_DA05-CON_AV05 ELSE 0 END AS CON_DA05,
			CASE WHEN CON_DA05<CON_AV05 THEN CON_AV05-CON_DA05 ELSE 0 END AS CON_AV05, 
			CASE WHEN CON_DA06>CON_AV06 THEN CON_DA06-CON_AV06 ELSE 0 END AS CON_DA06,
			CASE WHEN CON_DA06<CON_AV06 THEN CON_AV06-CON_DA06 ELSE 0 END AS CON_AV06, 
			CASE WHEN CON_DA07>CON_AV07 THEN CON_DA07-CON_AV07 ELSE 0 END AS CON_DA07,
			CASE WHEN CON_DA07<CON_AV07 THEN CON_AV07-CON_DA07 ELSE 0 END AS CON_AV07, 
			CASE WHEN CON_DA08>CON_AV08 THEN CON_DA08-CON_AV08 ELSE 0 END AS CON_DA08,
			CASE WHEN CON_DA08<CON_AV08 THEN CON_AV08-CON_DA08 ELSE 0 END AS CON_AV08, 
			CASE WHEN CON_DA09>CON_AV09 THEN CON_DA09-CON_AV09 ELSE 0 END AS CON_DA09,
			CASE WHEN CON_DA09<CON_AV09 THEN CON_AV09-CON_DA09 ELSE 0 END AS CON_AV09, 
			CASE WHEN CON_DA10>CON_AV10 THEN CON_DA10-CON_AV10 ELSE 0 END AS CON_DA10,
			CASE WHEN CON_DA10<CON_AV10 THEN CON_AV10-CON_DA10 ELSE 0 END AS CON_AV10, 
			CASE WHEN CON_DA11>CON_AV11 THEN CON_DA11-CON_AV11 ELSE 0 END AS CON_DA11,
			CASE WHEN CON_DA11<CON_AV11 THEN CON_AV11-CON_DA11 ELSE 0 END AS CON_AV11, 
			CASE WHEN CON_DA12>CON_AV12 THEN CON_DA12-CON_AV12 ELSE 0 END AS CON_DA12,
			CASE WHEN CON_DA12<CON_AV12 THEN CON_AV12-CON_DA12 ELSE 0 END AS CON_AV12 



FROM CONTI
WHERE LEN(CON_CONTO)>5
AND CON_IMP_D<>CON_IMP_A
AND CON_CONTO>='01' AND CON_CONTO<'30'
AND CON_SOC='01'
AND CON_ANNO='2012') AS T1
GROUP BY CON_CONTO,CON_DESCR
ORDER BY CON_CONTO



