
-- VISTA GRUPPI_MAIL_FORNITORI

IF  EXISTS (SELECT * FROM sys.views WHERE object_id = OBJECT_ID(N'[dbo].[GRUPPI_MAIL_FORNITORI]'))
	DROP VIEW [dbo].[GRUPPI_MAIL_FORNITORI]
GO

CREATE VIEW [dbo].[GRUPPI_MAIL_FORNITORI]  
AS
	SELECT
		FORN.FOR_CODCLI AS CODICE,
		FORN.FOR_RAGSOC AS RAG_SOCIALE,
		FORN.FOR_EMAIL AS IND_EMAIL_ANAGR,
		COALESCE(EML_GEN.INDIRIZZO_A,'') AS IND_EMAIL_A_GEN,
		COALESCE(EML_RDA.INDIRIZZO_A,'') AS IND_EMAIL_A_RDA,
		COALESCE(EML_ORF.INDIRIZZO_A,'') AS IND_EMAIL_A_ORF,
		COALESCE(EML_BFO.INDIRIZZO_A,'') AS IND_EMAIL_A_BFO,
		COALESCE(EML_REF.INDIRIZZO_A,'') AS IND_EMAIL_A_REF
	FROM U_FOR_AN AS FORN
	LEFT OUTER JOIN MAIL_ADDRESS AS EML_GEN ON EML_GEN.CLI_FOR = 'F' AND EML_GEN.CODICE_CLI_FOR = FORN.FOR_CODCLI AND EML_GEN.TIPO_DOC = 'GEN'
	LEFT OUTER JOIN MAIL_ADDRESS AS EML_RDA ON EML_RDA.CLI_FOR = 'F' AND EML_RDA.CODICE_CLI_FOR = FORN.FOR_CODCLI AND EML_RDA.TIPO_DOC = 'RDA'
	LEFT OUTER JOIN MAIL_ADDRESS AS EML_ORF ON EML_ORF.CLI_FOR = 'F' AND EML_ORF.CODICE_CLI_FOR = FORN.FOR_CODCLI AND EML_ORF.TIPO_DOC = 'ORF'
	LEFT OUTER JOIN MAIL_ADDRESS AS EML_BFO ON EML_BFO.CLI_FOR = 'F' AND EML_BFO.CODICE_CLI_FOR = FORN.FOR_CODCLI AND EML_BFO.TIPO_DOC = 'BFO'
	LEFT OUTER JOIN MAIL_ADDRESS AS EML_REF ON EML_REF.CLI_FOR = 'F' AND EML_REF.CODICE_CLI_FOR = FORN.FOR_CODCLI AND EML_REF.TIPO_DOC = 'REF'
	

