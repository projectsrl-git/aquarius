
/*
SCRIPT COMPLEMENTARE DI AGGIORNAMENTO TIPO SERIAL CODICI GRUPPI DI VENDITA IN ANAGRAFICA ARTICOLI
*/

UPDATE U_ART_AN SET ART_CODTSN = 'PRD' WHERE ART_CODPRI IN (
	SELECT DIS_GRUPPO FROM (
		SELECT DD.DIS_GRUPPO, ANC.ART_CODTSN AS ART_CODTSN_COMP, PA_TSN.ITALIANO AS MESSAGGIO FROM U_DIS_DD AS DD
		LEFT OUTER JOIN U_ART_AN AS ANC ON ANC.ART_CODPRI = DD.DIS_CODART
		LEFT OUTER JOIN U_ART_AN AS ANG ON ANG.ART_CODPRI = DD.DIS_GRUPPO
		LEFT OUTER JOIN PARA AS PA_TSN ON PA_TSN.CODICE = 'TSN'+ANC.ART_CODTSN
		WHERE ANC.ART_CODTSN <> '' AND ANG.ART_CODTSN = '' AND PA_TSN.ITALIANO LIKE '%RICHIEDE_SERIAL=SI%'
		) AS MAIN
	)

