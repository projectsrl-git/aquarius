	CASE MOV.MOV_SERV_INTRA WHEN '' THEN CEILING(COALESCE(PF.PESO_NETTO_ARTICOLI,0)) ELSE 0 END AS NEW_PESO_INTRA, PF.PESO_NETTO_ARTICOLI,
	MOV.MOV_PESO,

	PF.ORD_QTATOT,

	BE.LEG_AGGANC,
	BE.LEG_NUMPRN,
	BE.LEG_DESCRI, 
	BE.LEG_NUMDOC, 
	BE.LEG_DATDOC, 

	PF.ORD_CONDET,
	MOV.MOV_CONTO,
	PF.ORD_CCODET,
	MOV.MOV_CCO,
	PF.ART_CLDOG,
	MOV.MOV_CLDOG,
	PF.ART_SERVINTRA,
	MOV.MOV_SERV_INTRA,
	MOV.MOV_DESMOV, MOV.*
FROM MOV_CONT AS MOV
LEFT OUTER JOIN PARA AS PARA1 ON 'TOP'+MOV.MOV_TOP = PARA1.CODICE
LEFT OUTER JOIN PARA AS PARA2 ON 'IVA'+MOV.MOV_IVA = PARA2.CODICE
