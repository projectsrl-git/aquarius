LEFT OUTER JOIN CONTI AS CONTI ON MOV.MOV_CONTO = CONTI.CON_CONTO AND CONTI.CON_ANNO = MOV.MOV_ANNO AND CONTI.CON_TIPOCO='X'
LEFT OUTER JOIN (SELECT * FROM U_BFO_LE) AS BE ON BE.LEG_NUMPRO = MOV.MOV_NUMPRO AND BE.LEG_AGGANC <> '' AND BE.LEG_NUMPRN <> ''
LEFT OUTER JOIN (SELECT ord_valore, DAGGANCIO, cbf_ndoc, ORD_CONDET, ORD_CCODET, art_cldog, art_servintra, SUM(ART_PESOUM * ORD_QTAORD) AS PESO_NETTO_ARTICOLI, SUM(ORD_QTAORD) AS ORD_QTATOT FROM (

					SELECT PR.ART_PESOUM, PR.art_cldog, AN.art_servintra, BD.* FROM U_BFO_DD AS BD
