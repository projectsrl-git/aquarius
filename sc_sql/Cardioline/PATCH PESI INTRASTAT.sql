		CASE MOV.MOV_SERV_INTRA WHEN '' THEN CEILING(COALESCE(PF.PESO_NETTO_ARTICOLI,0)) ELSE 0 END AS NEW_PESO_INTRA,
		MOV.*
	FROM MOV_CONT AS MOV
	LEFT OUTER JOIN PARA AS PARA1 ON 'TOP'+MOV.MOV_TOP = PARA1.CODICE
