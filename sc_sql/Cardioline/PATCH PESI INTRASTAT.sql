						BD.ORD_DESART <> '*** COMMENTO ***'
					) AS MAIN
				GROUP BY ord_valore, DAGGANCIO, cbf_ndoc, ORD_CONDET, ORD_CCODET, art_cldog, art_servintra 
	) AS PF ON PF.DAGGANCIO = BE.LEG_AGGANC AND PF.cbf_ndoc = BE.LEG_NUMPRN AND PF.ORD_VALORE = MOV.MOV_IMP and PF.ORD_CONDET = MOV.MOV_CONTO AND PF.ORD_CCODET = MOV.MOV_CCO AND PF.art_cldog = MOV.MOV_CLDOG AND PF.art_servintra = MOV.MOV_SERV_INTRA
WHERE 
	--MOV_NUMPRO = '_4A9110Y5K' AND 
	MOV_DTREG BETWEEN '2015/05/01' AND '2015/05/31'
	AND MOV_TOP <> 'CHI'
	AND MOV_CONTABILITA_PREVISIONALE<>'P'
	AND SUBSTRING(PARA1.LIBERA,24,1)='S'
	AND SUBSTRING(PARA1.LIBERA,12,1)='F'  -- CLIENTE/FORNITORE
	AND SUBSTRING(PARA1.LIBERA,2,1)='S'
	AND SUBSTRING(PARA1.LIBERA,23,1)='E'
	AND SUBSTRING(PARA1.LIBERA,11,1)<>'S'
	AND CONTI.CON_TIPOCO='X'
	AND (SUBSTRING(PARA2.ITALIANO,1,100) = 'BENI-INTRASTAT' OR SUBSTRING(PARA2.ITALIANO,1,100) = 'SERVIZI-INTRASTAT')
ORDER BY MOV_NREGIS


