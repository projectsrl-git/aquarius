
-- VISTA DISTINTA_BASE_PER_LINEA_PRODOTTO
IF  EXISTS (SELECT * FROM sys.views WHERE object_id = OBJECT_ID(N'[dbo].[DISTINTA_BASE_PER_LINEA_PRODOTTO]'))
	DROP VIEW [dbo].[DISTINTA_BASE_PER_LINEA_PRODOTTO]
GO
CREATE VIEW [dbo].[DISTINTA_BASE_PER_LINEA_PRODOTTO]  
AS

SELECT 
	DIT_GRUPPO AS COD_GRUPPO,
	A1.ART_DESCR AS DESCR_GRUPPO,
	A1.ART_LINEA AS COD_LINEA_GRUPPO,
	COALESCE(PL1.DESCRI,'') AS DESCR_LINEA_GRUPPO,
	DIS_SEQUEN AS NR_RIGA,
	DIS_CODART AS COD_COMPONENTE,
	A2.ART_DESCR AS DESCR_COMPONENTE,
	A2.ART_LINEA AS COD_LINEA_COMPONENTE,
	COALESCE(PL2.DESCRI,'') AS DESCR_LINEA_COMPONENTE,
	DIS_QTA AS QUANTITA,
	DIS_UM AS UM,
	DIS_FITTIZ AS FITTIZIO
FROM U_DIS_TT AS TT
LEFT OUTER JOIN U_DIS_DD AS DD ON DD.DAGGANCIO = TT.TAGGANCIO
LEFT OUTER JOIN U_ART_PR AS A1 ON A1.ART_CODPRI = TT.DIT_GRUPPO
LEFT OUTER JOIN PARA AS PL1 ON PL1.CODICE = 'REP'+A1.ART_LINEA
LEFT OUTER JOIN U_ART_PR AS A2 ON A2.ART_CODPRI = DD.DIS_CODART
LEFT OUTER JOIN PARA AS PL2 ON PL2.CODICE = 'REP'+A2.ART_LINEA

GO

SELECT * FROM DISTINTA_BASE_PER_LINEA_PRODOTTO ORDER BY COD_GRUPPO,NR_RIGA