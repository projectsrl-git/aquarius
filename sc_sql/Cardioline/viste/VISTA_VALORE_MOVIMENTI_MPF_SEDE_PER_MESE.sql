
IF  EXISTS (SELECT * FROM sys.views WHERE object_id = OBJECT_ID(N'[dbo].[VALORE_MOVIMENTI_MPF_SEDE_PER_MESE]'))
	DROP VIEW [dbo].[VALORE_MOVIMENTI_MPF_SEDE_PER_MESE]
