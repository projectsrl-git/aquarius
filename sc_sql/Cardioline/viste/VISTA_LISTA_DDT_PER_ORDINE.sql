
-- VISTA LISTA_DDT_PER_ORDINE

IF  EXISTS (SELECT * FROM sys.views WHERE object_id = OBJECT_ID(N'[dbo].[LISTA_DDT_PER_ORDINE]'))
	DROP VIEW [dbo].[LISTA_DDT_PER_ORDINE]
GO

CREATE VIEW [dbo].[LISTA_DDT_PER_ORDINE]  
AS
	WITH TEMP AS
	(
	SELECT
		ORS_NUMORC AS NUMORC,
		ORS_DATORC AS DATORC,
		ORS_NUMORD AS NUMDDT,
		ORS_DATORD AS DATDDT
	FROM (
		SELECT ORS_NUMORD, ORS_DATORD, ORS_NUMORC, ORS_DATORC
		FROM U_BOL_DD
		WHERE ORD_CODART <> '' AND ORS_NUMORC <> '' AND ORS_DATORC <> ''
		GROUP BY ORS_NUMORD, ORS_DATORD, ORS_NUMORC, ORS_DATORC
		) AS MAIN WHERE ORS_NUMORC <> '' AND ORS_DATORC <> ''
	)

	select NUMORC, DATORC,
		SUBSTRING((Select ','+NUMDDT from TEMP AS T1 where T1.NUMORC=T2.NUMORC AND T1.DATORC=T2.DATORC FOR XML PATH('')),2,254) AS NUMDDT,
		SUBSTRING((Select ','+DATDDT from TEMP AS T1 where T1.NUMORC=T2.NUMORC AND T1.DATORC=T2.DATORC FOR XML PATH('')),2,254) AS DATDDT
	from TEMP AS T2
	group by NUMORC, DATORC

GO
