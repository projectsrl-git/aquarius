-- VISTA ECONOMICI_PER_CENTRO_COSTO_2026

IF  EXISTS (SELECT * FROM sys.views WHERE object_id = OBJECT_ID(N'[dbo].[ECONOMICI_PER_CENTRO_COSTO_MESE_2026]'))
	DROP VIEW [dbo].[ECONOMICI_PER_CENTRO_COSTO_MESE_2026]
GO

CREATE VIEW [dbo].[ECONOMICI_PER_CENTRO_COSTO_MESE_2026]  
AS

SELECT ANNO,MESE,CONTO,DESCR_CONTO,CENTRO_COSTO,CAST(SUM(CASE WHEN MOV_TMOV='D' THEN 1 ELSE -1 END * MOV_IMP * MOV_CAMBIO) AS NUMERIC(19,2)) AS IMPORTO FROM (
	SELECT 
		ECO.MOV_ANNO AS ANNO,
		substring(ECO.MOV_DTREG,6,2) AS MESE,
		ECO.MOV_CONTO AS CONTO,
		SUBSTRING(COALESCE(CONTI.CON_DESCR,''),1,50) AS DESCR_CONTO,
		ECO.MOV_CCO AS CENTRO_COSTO,
		ECO.MOV_TMOV,
		ECO.MOV_IMP,
		ECO.MOV_CAMBIO
	FROM MOV_CONT AS ECO
	INNER JOIN CONTI ON ECO.MOV_CONTO=CONTI.CON_CONTO AND ECO.MOV_ANNO=CONTI.CON_ANNO
	where ECO.MOV_ANNO = '2026'
	) AS MAIN
GROUP BY ANNO,MESE,CONTO,DESCR_CONTO,CENTRO_COSTO


GO