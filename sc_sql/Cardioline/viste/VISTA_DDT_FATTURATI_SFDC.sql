
IF  EXISTS (SELECT * FROM sys.views WHERE object_id = OBJECT_ID(N'[dbo].[DDT_FATTURATI_SFDC]'))
	DROP VIEW [dbo].[DDT_FATTURATI_SFDC]
GO

CREATE VIEW [dbo].[DDT_FATTURATI_SFDC]  
AS

	SELECT
		SUBSTRING(TT.ORD_DATORD,1,4) AS ANNO,
		SUBSTRING(TT.ORD_DATORD,6,2) AS MESE,
		DD.DAGGANCIO+DD.ORD_CODSYS AS EXTERNAL_ID_RIGA_DDT_SFDC,
		TT.ORD_DATORD AS DATA_DDT,
		TT.ORD_NUMORD AS NUM_DDT,
		DD.DAGGANCIO AS EXTERNAL_ID_TESTATA_DDT_SFDC,
		DD.ORD_SEQUEN AS N_RIGA, 
		DD.ORD_CODART AS PRODOTTO,
		DD.ORD_DESART AS DESCRIZIONE,
		DD.ORD_PRZNET * TT.ORD_CAMBIO AS PRZ_NETTO,
		DD.ORD_QTAORD AS QTA,
		DD.ORD_VALORE AS VALORE,
		COALESCE(OD.ORS_NUMORD,'') AS NUM_ORDINE,
		COALESCE(OD.ORS_DATORD,'') AS DATA_ORDINE,
		COALESCE(FD.ORS_NUMORD,'') AS NUM_FATTURA,
		COALESCE(FD.ORS_DATORD,'') AS DATA_FATTURA,
		CAST((CASE COALESCE(FD.ORS_NUMORD,'') WHEN '' THEN 0 ELSE 1 END) AS BIT) AS FATTURATO
	FROM U_BOL_DD AS DD
	LEFT OUTER JOIN U_BOL_TT AS TT ON TT.TAGGANCIO = DD.DAGGANCIO
	LEFT OUTER JOIN U_ORD_DD AS OD ON OD.ORD_CODART = DD.ORD_CODART AND OD.ORD_LEGSYS = DD.ORD_LEGSYS
	LEFT OUTER JOIN U_FAT_DD AS FD ON DD.ORS_SYSFAT <> '' AND DD.ORS_SYSFAT = FD.ORS_SYSDDT
	WHERE TT.ORD_CAUS IN ('VEN','VEG') AND DD.ORD_CODART <> '' AND DD.ORD_DESART <> '*** COMMENTO ***'

GO


SELECT * FROM DDT_FATTURATI_SFDC WHERE ANNO = '2023' AND MESE = '06' ORDER BY EXTERNAL_ID_TESTATA_DDT_SFDC,N_RIGA