CREATE VIEW [dbo].[FATTURATO_FORNITORI_PER_CONTO_CONTABILE]  
AS
	SELECT
		SUBSTRING(IND.MOV_DTDOC,1,4) AS Anno,
		SUBSTRING(IND.MOV_DTDOC,6,2) AS Mese,
		IND.MOV_DTDOC AS Data_documento,
		IND.MOV_NPROT AS Nr_Fattura,
		IND.MOV_NPROT AS Nr_Protocollo_IVA,
		IND.MOV_TOP AS Tipo_Operazione,
		IND.MOV_NREGIS AS Nr_Registrazione,
		IND.MOV_DTREG AS Data_Registrazione,
		SUBSTRING(IND.MOV_CONTO,5,10) AS Codice_Fornitore,
		COALESCE(CONTO_FOR.CON_DESCR,'') AS Ragione_Sociale,
		COALESCE(MOV.MOV_CONTO,'') AS Conto_contabile,
		COALESCE(CONTI.CON_DESCR,'') AS Descrizione_Conto_contabile,
		IND.MOV_VALUTA AS Valuta,
		CAST(ROUND(CASE MOV.MOV_TMOV WHEN 'D' THEN COALESCE(MOV.MOV_IMP,0) ELSE 0 END,2) AS NUMERIC(19,2)) AS Importo_DARE,
		CAST(ROUND(CASE MOV.MOV_TMOV WHEN 'A' THEN COALESCE(MOV.MOV_IMP,0) ELSE 0 END,2) AS NUMERIC(19,2)) AS Importo_AVERE,
		CAST(ROUND(COALESCE(MOV.MOV_IMPOST,0),2) AS NUMERIC(19,2)) AS Importo_Imposta,
		COALESCE(MOV.MOV_IVA,'') AS Codice_IVA,
		COALESCE(PA_IVA.DESCRI,'') AS Descrizione_Codice_IVA,
		COALESCE(MOV.MOV_CCO,'') AS Centro_di_costo,
		IND.MOV_CAMBIO AS Cambio,
		CAST(ROUND(CASE MOV.MOV_TMOV WHEN 'D' THEN COALESCE(MOV.MOV_IMP,0) ELSE 0 END * IND.MOV_CAMBIO,2) AS NUMERIC(19,2)) AS Importo_DARE_EUR,
		CAST(ROUND(CASE MOV.MOV_TMOV WHEN 'A' THEN COALESCE(MOV.MOV_IMP,0) ELSE 0 END * IND.MOV_CAMBIO,2) AS NUMERIC(19,2)) AS Importo_AVERE_EUR,
		CAST(ROUND(COALESCE(MOV.MOV_IMPOST,0),2) * IND.MOV_CAMBIO AS NUMERIC(19,2)) AS Importo_Imposta_EUR
	FROM INDICE AS IND
	LEFT OUTER JOIN MOV_CONT AS MOV ON MOV.MOV_ANNO = IND.MOV_ANNO AND MOV.MOV_NUMPRO = IND.MOV_NUMPRO AND MOV.MOV_CONTO <> IND.MOV_CONTO
	LEFT OUTER JOIN CONTI ON CONTI.CON_ANNO = MOV.MOV_ANNO AND CONTI.CON_CONTO = MOV.MOV_CONTO
	LEFT OUTER JOIN CONTI as CONTO_FOR ON CONTO_FOR.CON_ANNO = IND.MOV_ANNO AND CONTO_FOR.CON_CONTO = IND.MOV_CONTO
	LEFT OUTER JOIN PARA AS PA_IVA ON PA_IVA.CODICE = 'IVA'+MOV.MOV_IVA
	WHERE 
		IND.MOV_TOP IN (SELECT SUBSTRING(CODICE,4,3) FROM PARA WHERE CODICE LIKE 'TOP%' AND SUBSTRING(LIBERA,2,1) = 'S' AND SUBSTRING(LIBERA,12,1) = 'F') AND 
		CONTI.CON_TIPOCO <> 'I'
GO

