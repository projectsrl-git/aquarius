-- VISTA ODINATO PER REPORT

IF  EXISTS (SELECT * FROM sys.views WHERE object_id = OBJECT_ID(N'[dbo].[ORDINATO_PER_REPORT]'))
	DROP VIEW [dbo].[ORDINATO_PER_REPORT]
GO

CREATE VIEW [dbo].[ORDINATO_PER_REPORT]  
AS
	SELECT 
		ORD_CCODET AS CENTRO_COSTO,
		CDC.CCODESCR AS DESCR_CENTRO_COSTO,
		CDC.TIPO_CCOSTO AS TIPO_CCOSTO,
		P5.DESCRI AS DESCR_TIPO_CCOSTO,
		CLI.CLI_ZONA AS ZONA,
		P1.DESCRI AS DESC_ZONA,
		CLI.CLI_NAZION AS NAZIONE,
		P2.DESCRI AS DESC_NAZIONE,
		CLI.CLI_PROVIN AS PROVINCIA,
		P3.DESCRI AS DESC_PROVINCIA,
		CLI.CLI_CANVEN SALES_CHANNEL,
		P4.DESCRI AS DESC_SALES_CHANNEL,
		ORS_CODCLI AS CODCLI,
		CLI_RAGSOC AS RAGSOC,
		ORS_DATORD AS DATA_ORDINE,
		SUBSTRING(ORS_DATORD,1,4) AS ANNO,
		SUBSTRING(ORS_DATORD,6,2) AS MESE,
		ORD_CODART AS CODICE_ART,
		ART_DESCR  AS DESCR_ART,
		ORD_VALORE AS VALORE
	FROM U_ORD_DD AS ORD
	LEFT OUTER JOIN U_ART_PR AS ART
	ON ORD_CODART = ART_CODPRI
	LEFT OUTER JOIN U_CLI_AN AS CLI
	ON ORS_CODCLI = CLI_CODCLI
	LEFT OUTER JOIN (SELECT DISTINCT CON_CONTO AS CCOSTO, CON_DESCR AS CCODESCR, TIPO_CCOSTO  FROM CCOSTO ) AS CDC
	ON ORD_CCODET= CCOSTO
	LEFT OUTER JOIN PARA AS P1
	ON 'ZON'+CLI.CLI_ZONA = P1.CODICE
	LEFT OUTER JOIN PARA AS P2
	ON 'STA'+CLI.CLI_NAZION = P2.CODICE
	LEFT OUTER JOIN PARA AS P3
	ON 'PRO'+CLI.CLI_PROVIN = P3.CODICE
	LEFT OUTER JOIN PARA AS P4
	ON 'CVE'+CLI.CLI_CANVEN = P4.CODICE
	LEFT OUTER JOIN PARA AS P5
	ON 'TCC'+CDC.TIPO_CCOSTO = P5.CODICE
	WHERE ORD_QTAORD<>0
GO



