CREATE VIEW [dbo].[SALDI_CONTABILI_CLI_FOR_202311]  
AS

SELECT
	CON_ANNO AS ANNO,
	CON_TIPOCO AS TIPO_CONTO,
	CON_CONTO AS CONTO,
	CON_DESCR AS DESCRIZIONE,
	COALESCE(SUM(CASE WHEN MOV_TOP = 'APE' THEN (CASE WHEN MOV_TMOV='D' THEN 1 ELSE -1 END * MOV_IMP * MOV_CAMBIO) ELSE 0 END),0) AS SALDO_APE,
	COALESCE(SUM(CASE WHEN MOV_TOP <> 'APE' THEN (CASE WHEN MOV_TMOV='D' THEN 1 ELSE 0 END * MOV_IMP * MOV_CAMBIO) ELSE 0 END),0) AS DARE,
	COALESCE(SUM(CASE WHEN MOV_TOP <> 'APE' THEN (CASE WHEN MOV_TMOV='A' THEN 1 ELSE 0 END * MOV_IMP * MOV_CAMBIO) ELSE 0 END),0) AS AVERE,
	COALESCE(SUM(CASE WHEN MOV_TMOV='D' THEN 1 ELSE -1 END * MOV_IMP * MOV_CAMBIO),0) AS SALDO
FROM CONTI 
LEFT OUTER JOIN MOV_CONT  AS MOV
ON CON_CONTO=MOV_CONTO AND CON_ANNO=MOV_ANNO
WHERE CON_TIPOCO IN ('F') AND MOV_DTREG >= '2023/01/01' AND MOV_DTREG <= '2023/11/30'
GROUP BY CON_ANNO,CON_TIPOCO,CON_CONTO,CON_DESCR


GO


