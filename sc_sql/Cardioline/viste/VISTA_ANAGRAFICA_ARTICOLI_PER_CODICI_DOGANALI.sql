
-- VISTA ANAGRAFICA_ARTICOLI_PER_CODICI_DOGANALI
IF  EXISTS (SELECT * FROM sys.views WHERE object_id = OBJECT_ID(N'[dbo].[ANAGRAFICA_ARTICOLI_PER_CODICI_DOGANALI]'))
	DROP VIEW [dbo].[ANAGRAFICA_ARTICOLI_PER_CODICI_DOGANALI]
GO
CREATE VIEW [dbo].[ANAGRAFICA_ARTICOLI_PER_CODICI_DOGANALI] 
AS
	SELECT
		PR.ART_CODPRI AS COD_ARTICOLO,
		PR.ART_DESCR AS DESCRIZIONE,
		PR.ART_CLDOG AS CLASSE_DOGANALE,
		COALESCE(P_DOG.DESCRI,'') AS DESCR_CLASSE_DOGANALE,
		AN.art_servintra AS COD_SERVIZI_INTRA,
		COALESCE(P_SEI.DESCRI,'') AS DESCR_COD_SERVIZI_INTRA,
		PR.ART_MAGA AS COD_MAG,
		PR.ART_PRZVEN AS LISTINO_ITALIA,
		PR.ART_PRZVE2 AS LISTINO_INTERNATIONAL,

		SUBSTRING(AN.ART_CODGMI,1,3) AS GRUPPO,
		COALESCE(CCODGRU.DESCRI,'') AS DESGRUPPO,
		SUBSTRING(AN.ART_CODGMI,1,6) AS FAMIGLIA,
		COALESCE(CCODFAM.DESCRI,'') AS DESFAMIGLIA,
		AN.ART_CODGMI AS SOTTOFAMIGLIA,
		COALESCE(CCODMERC.DESCRI,'') AS DESSOTTOFAMIGLIA,

		AN.ART_CODGMI AS COD_GRUPPO_MERCEOLOGICO_INDUSTRIALE,
		COALESCE(P_GMI.DESCRI,'') AS DESCR_GRUPPO_MERCEOLOGICO_INDUSTRIALE,
		PR.ART_SERIE AS COD_TIPO_FATT_ELETTRONICA,
		COALESCE(P_SER.DESCRI,'') AS DESCR_TIPO_FATT_ELETTRONICA
	FROM U_ART_PR AS PR
	LEFT OUTER JOIN U_ART_AN AS AN ON AN.ART_CODPRI = PR.ART_CODPRI
	LEFT OUTER JOIN PARA AS P_DOG ON P_DOG.CODICE = 'DOG'+PR.ART_CLDOG 
	LEFT OUTER JOIN PARA AS P_SEI ON P_SEI.CODICE = 'SEI'+AN.art_servintra 
	LEFT OUTER JOIN PARA AS P_GMI ON P_GMI.CODICE = 'GMI'+AN.ART_CODGMI 
	LEFT OUTER JOIN PARA AS CCODGRU ON CCODGRU.CODICE = 'GMI'+SUBSTRING(AN.ART_CODGMI,1,3)
	LEFT OUTER JOIN PARA AS CCODFAM ON CCODFAM.CODICE = 'GMI'+SUBSTRING(AN.ART_CODGMI,1,6)
	LEFT OUTER JOIN PARA AS CCODMERC ON CCODMERC.CODICE = 'GMI'+AN.ART_CODGMI
	LEFT OUTER JOIN PARA AS P_SER ON P_SER.CODICE = 'SER'+PR.ART_SERIE 
	WHERE 
		ART_OBSOL <> 'S'

GO