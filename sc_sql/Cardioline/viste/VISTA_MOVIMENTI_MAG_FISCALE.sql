-- VISTA MOVIMENTI_MAG_FISCALE

IF  EXISTS (SELECT * FROM sys.views WHERE object_id = OBJECT_ID(N'[dbo].[MOVIMENTI_MAG_FISCALE]'))
	DROP VIEW [dbo].[MOVIMENTI_MAG_FISCALE]
GO

CREATE VIEW [dbo].[MOVIMENTI_MAG_FISCALE]  
AS

SELECT
	SUBSTRING(MOV_DTREGI,1,4) AS ANNO,
	MOV_DTREGI AS DATA_REG,
	MOV_ORAMOV AS ORA_REG,
	MOV_CODMAG AS COD_MAG,
	COALESCE(PARA_MAG.DESCRI,'') AS DES_MAG,
	MOV_NUMDOC AS NUM_DOC,
	MOV_DTDOCU AS DATA_DOC,
	MOV_TOP AS COD_CAUS,
	COALESCE(PARA_TOP.DESCRI,'') AS DES_CAUS,
	MOV_SEGNO AS SEGNO,
	MOV_CODART AS COD_ART,
	COALESCE(U_ART_PR.ART_DESCR,'') AS DES_ART,
	MOV_QTAMOV,
	COALESCE(U_ART_PR.ART_UMMAGA,'') AS UM,
	MOV_PREACQ AS PREZZO,
	COALESCE(MV.MOV_VALUTA_1ST,'EUR') AS VALUTA,
	COALESCE(VV.VVA_CHANGE,1) AS CAMBIO,
	MOV_QTAMOV * MOV_PREACQ * COALESCE(VV.VVA_CHANGE,1) AS VALORE
FROM U_MAG_MO AS T1 
LEFT OUTER JOIN U_ART_PR ON MOV_ANAART = ART_CODPRI 
LEFT OUTER JOIN PARA AS PARA_MAG ON PARA_MAG.CODICE = 'MAG'+MOV_CODMAG 
LEFT OUTER JOIN PARA AS PARA_TOP ON PARA_TOP.CODICE = 'TOP'+MOV_TOP 
LEFT OUTER JOIN PARA AS PARA_CPA ON PARA_CPA.CODICE = 'CPA'+MOV_PAGAME  
OUTER APPLY (SELECT TOP 1 MOV_LEGAME AS MOV_LEGAME_1ST, MOV_DTREGI AS MOV_DTREGI_1ST, MOV_VALUTA AS MOV_VALUTA_1ST, MOV_DTDOCU AS MOV_DTDOCU_1ST, 
MOV_DTSCAD AS MOV_DTSCAD_1ST FROM U_MAG_MO WHERE MOV_LEGAME = T1.MOV_LEGAME ORDER BY MOV_DTREGI,MOV_ORAMOV) AS MV
OUTER APPLY (SELECT TOP 1 * FROM U_VVA_CH WHERE VVA_CODVAL = MV.MOV_VALUTA_1ST AND VVA_DATA <= MV.MOV_DTDOCU_1ST ORDER BY VVA_DATA DESC) AS VV
