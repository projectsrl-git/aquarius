
/****** Object:  View [dbo].[SALDI_CONTABILI_PER_ANNO_CHIUSO]    Script Date: 23/03/2017 16:17:16 ******/
IF  EXISTS (SELECT * FROM sys.views WHERE object_id = OBJECT_ID(N'[dbo].[SALDI_CONTABILI_PER_ANNO_CHIUSO]'))
	DROP VIEW [dbo].[SALDI_CONTABILI_PER_ANNO_CHIUSO]
GO
CREATE VIEW [dbo].[SALDI_CONTABILI_PER_ANNO_CHIUSO]  
AS

SELECT
	ANNO,
	TIPO,
	CONTO,
	DESCR_CONTO,
	-1*ROUND(SUM(MOV_IMP_DA),2) AS SALDO
FROM (
	SELECT
		MOV_ANNO AS ANNO,
		CASE CON_POSBIL WHEN 'P' THEN 'PATRIMONIALE' WHEN 'E' THEN 'ECONOMICO' ELSE '' END AS TIPO,
		MOV_CONTO AS CONTO,
		CON_DESCR AS DESCR_CONTO,
		(CASE MOV_TMOV WHEN 'D' THEN 1 ELSE -1 END) * MOV_IMP AS MOV_IMP_DA
	FROM MOV_CONT
	LEFT OUTER JOIN CONTI ON CON_CONTO = MOV_CONTO AND CON_ANNO = MOV_ANNO
	WHERE
		MOV_TOP = 'CHI'
	) AS MAIN
GROUP BY ANNO,TIPO,CONTO,DESCR_CONTO

GO
