
-- VISTA RDA_FORNITORI
IF  EXISTS (SELECT * FROM sys.views WHERE object_id = OBJECT_ID(N'[dbo].[RDA_FORNITORI]'))
	DROP VIEW [dbo].[RDA_FORNITORI]
GO
CREATE VIEW [dbo].[RDA_FORNITORI]  
AS

SELECT
	SUBSTRING(DATA,1,4) AS ANNO,
	SUBSTRING(DATA,6,2) AS MESE,
	CAST(DATEPART(WEEK,CAST(DATA AS date)) AS NUMERIC(2,0)) AS SETTIMANA,
	TT.NUMERO,
	TT.DATA,
	TT.CODRIC,
	DD.DESART,
	DD.CODFOR,
	DD.DESFOR,
	COALESCE(FO.FOR_CONPAG,'') AS COND_PAG,
	COALESCE(P_CPA.DESCRI,'') AS DESCR_COND_PAG,
	COALESCE(FO.FOR_DIVISIONE,'') AS QUALIF_DIVISIONE,
	COALESCE(P_DIF.DESCRI,'') AS DESCR_QUALIF_DIVISIONE,
	CASE COALESCE(FO.FOR_ALRID,'') WHEN '' THEN COALESCE(FO.FOR_CODIVA,'') ELSE COALESCE(FO.FOR_ALRID,'') END AS COD_IVA,
	CASE COALESCE(FO.FOR_ALRID,'') WHEN '' THEN COALESCE(P_IVA.DESCRI,'') ELSE COALESCE(P_ALRID.DESCRI,'') END AS DESCR_IVA,
	DD.UNIMIS,
	DD.QUANTITA,
	DD.VALORE,
	CAST(ROUND(DD.VALORE * (1 + ((CAST(CASE COALESCE(FO.FOR_ALRID,'') WHEN '' THEN COALESCE(SUBSTRING(P_IVA.LIBERA,1,3),'0') ELSE COALESCE(SUBSTRING(P_ALRID.LIBERA,1,3),'0') END AS NUMERIC(9,2))) / 100)),2) AS NUMERIC(19,2)) AS VALORE_IVATO,
	TT.DATACHIU,
	DD.COMMENTO,
	TT.DATVIS1,
	TT.DATVIS1 AS VISTO_RESPONS,
	TT.ESIVIS1 AS ESITO_VISTO_RESPONS,
--NOME COGNOME RESPONSABILE CHE HA AUTORIZZATO RDA
	TT.CCOSTOT AS C_COSTO,
	TT.DES_CCOSTOT AS DESCR_C_COSTO,
	TT.DATACHIU AS DATA_CHIUSURA,
	TT.RIFDDT AS RIF_DDT,
	TT.RIFFAT AS RIF_FAT,
	DD.DATCON as DATA_CONSEGNA
FROM RDA_TT AS TT
LEFT OUTER JOIN RDA_DD AS DD ON DD.DAGGANCIO = TT.TAGGANCIO
LEFT OUTER JOIN U_FOR_AN AS FO ON FO.FOR_CODCLI = DD.CODFOR
LEFT OUTER JOIN PARA AS P_CPA ON P_CPA.CODICE = 'CPA'+FO.FOR_CONPAG
LEFT OUTER JOIN PARA AS P_DIF ON P_DIF.CODICE = 'DIF'+FO.FOR_DIVISIONE
LEFT OUTER JOIN PARA AS P_IVA ON P_IVA.CODICE = 'IVA'+FO.FOR_CODIVA
LEFT OUTER JOIN PARA AS P_ALRID ON P_ALRID.CODICE = 'IVA'+FO.FOR_ALRID

GO

SELECT * FROM RDA_FORNITORI ORDER BY DATA,NUMERO