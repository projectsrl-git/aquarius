-- VISTA ESTR_GRUPPI_E_MAIL_CLIENTI
IF  EXISTS (SELECT * FROM sys.views WHERE object_id = OBJECT_ID(N'[dbo].[ESTR_GRUPPI_E_MAIL_CLIENTI]'))
	DROP VIEW [dbo].[ESTR_GRUPPI_E_MAIL_CLIENTI]
GO
CREATE VIEW [dbo].[ESTR_GRUPPI_E_MAIL_CLIENTI]  
AS
	SELECT
		MAIL.CODICE_CLI_FOR,
		AN.CLI_RAGSOC,
		MAIL.TIPO_DOC,
		CASE 
		   WHEN MAIL.TIPO_DOC = 'GEN'
			  THEN 'GENERALE'
		   WHEN MAIL.TIPO_DOC = 'OFF'
			  THEN 'OFFERTE CLIENTI'
		   WHEN MAIL.TIPO_DOC = 'ORD'
			  THEN 'ORDINI CLIENTI'
		   WHEN MAIL.TIPO_DOC = 'BOL'
			  THEN 'DOCUMENTI DI CONSEGNA A CLIENTI'  
		   WHEN MAIL.TIPO_DOC = 'FAP'
			  THEN 'FATTURE PROFORMA'
		   WHEN MAIL.TIPO_DOC = 'FAT'
			  THEN 'FATTURE CLIENTI / NOTE DI CREDITO'
		   WHEN MAIL.TIPO_DOC = 'ORT'
			  THEN 'ORDINI CLIENTI DI TRANSITO'
		   WHEN MAIL.TIPO_DOC = 'REC'
			  THEN 'BOLLE DI RESO DA CLIENTI'
		   WHEN MAIL.TIPO_DOC = 'SOL'
			  THEN 'SOLLECITI'
		   ELSE ''
		END AS DESCR_TIPO_DOC,
		MAIL.INDIRIZZO_A,
		MAIL.INDIRIZZO_CC,
		MAIL.INDIRIZZO_CCN,
		MAIL.OGGETTO,
		MAIL.CORPO,
		MAIL.CONFERMA
	FROM MAIL_ADDRESS AS MAIL
	INNER JOIN U_CLI_AN AS AN ON AN.CLI_CODCLI = MAIL.CODICE_CLI_FOR
	WHERE MAIL.CLI_FOR = 'C'

GO




