
-- VISTA ESTR_ANAGRAFICA_CLIENTI
IF  EXISTS (SELECT * FROM sys.views WHERE object_id = OBJECT_ID(N'[dbo].[ESTR_ANAGRAFICA_CLIENTI]'))
	DROP VIEW [dbo].[ESTR_ANAGRAFICA_CLIENTI]
GO
CREATE VIEW [dbo].[ESTR_ANAGRAFICA_CLIENTI]  
AS
	SELECT
		AN.CLI_CODCLI,
		AN.CLI_RAGSOC,
		AN.CLI_INDIR,
		AN.CLI_LOCALI,
		AN.CLI_PROVIN,
		AN.CLI_CAP,
		COALESCE(REGIONI.REGIONE,'') AS REGIONE,
		AN.CLI_NAZION,
		COALESCE(PARA_STA.DESCRI,'') AS DES_NAZIONE,
		AN.CLI_ZONA,
		COALESCE(PARA_ZON.DESCRI,'') AS DES_ZONA,
		AN.CLI_CONPAG,
		COALESCE(PARA_CPA.DESCRI,'') AS DES_CONPAG,
		AN.CLI_CANVEN,
		COALESCE(PARA_CVE1.DESCRI,'') AS CANALE_VENDITA,
		COALESCE(PARA_CVE2.DESCRI,'') AS MACROCLASSE,
		COALESCE(PARA_CVE1.DESCRI,'') AS CLASSE,
		AN.CLI_CODFIS,
		AN.CLI_PARIVA,
		AN.CLI_IVACEE,
		AN.CLI_TELEFO,
		AN.CLI_FAX,
		AN.CLI_TELEX,
		AN.CLI_EMAIL,
		AN.CLI_HOMPAG,
		AN.CLI_LINGUA,
		COALESCE(PA_LNG.DESCRI,'') AS CLI_LINGUA_DES
	FROM U_CLI_AN AS AN
	LEFT OUTER JOIN U_ANA_CL AS CL ON CL.CLI_CODCLI = AN.CLI_CODCLI
	LEFT OUTER JOIN PARA AS PA_LNG ON 'LNG'+AN.CLI_LINGUA = PA_LNG.CODICE
	LEFT OUTER JOIN PROVINCE ON PROVINCE.SIGLA = AN.CLI_PROVIN
	LEFT OUTER JOIN REGIONI ON REGIONI.CODICE = PROVINCE.REGIONE
	LEFT OUTER JOIN PARA AS PARA_STA ON PARA_STA.CODICE = 'STA'+AN.CLI_NAZION
	LEFT OUTER JOIN PARA AS PARA_ZON ON PARA_ZON.CODICE = 'ZON'+AN.CLI_ZONA
	LEFT OUTER JOIN PARA AS PARA_CPA ON PARA_CPA.CODICE = 'CPA'+AN.CLI_CONPAG
	LEFT OUTER JOIN PARA AS PARA_CVE1 ON PARA_CVE1.CODICE = 'CVE'+SUBSTRING(AN.CLI_CANVEN,1,3)
	LEFT OUTER JOIN PARA AS PARA_CVE2 ON PARA_CVE2.CODICE = 'CVE'+SUBSTRING(AN.CLI_CANVEN,1,6)
	LEFT OUTER JOIN PARA AS PARA_CVE3 ON PARA_CVE3.CODICE = 'CVE'+SUBSTRING(AN.CLI_CANVEN,1,9)
	LEFT OUTER JOIN CCOSTO ON CCOSTO.CON_ANNO = '2018' AND CCOSTO.CON_CONTO = AN.CLI_CCOST1

GO
