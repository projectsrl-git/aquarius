	DROP VIEW [dbo].[MOVIMAG_AQ]
GO

CREATE VIEW [dbo].[MOVIMAG_AQ]  
AS
	SELECT
		SUBSTRING(ORD_CODART,1,16) AS ARTICOLO,
		PR.ART_DESCR AS DESARTICOL,
		PR.ART_BARCLI AS BARCODE,
		CAST(ORD_QTAORD AS INT) AS QUANTITA,
		'ORD'+ORD_D.DAGGANCIO AS NUMERO,
		'ORD'+ORD_D.ORD_CODSYS AS RIGA,
		CASE
			WHEN COALESCE(BOL_LEG.DAGGANCIO,'') <> '' THEN 'BOL'+BOL_LEG.DAGGANCIO
			ELSE NULL
		END AS NUMERORIF,
		ORD_PESOUM AS PESOKG,
		ORD_UM AS UNIMISURA,
		CAST(ORD_QTAORD AS INT) AS QTACQUISTO
	FROM U_ORD_DD AS ORD_D
	LEFT OUTER JOIN (SELECT DAGGANCIO, ORD_LEGSYS FROM U_BOL_DD) AS BOL_LEG ON BOL_LEG.ORD_LEGSYS = ORD_D.ORD_LEGSYS
	LEFT OUTER JOIN U_ART_PR AS PR ON PR.ART_CODPRI = ORD_D.ORD_CODART
	WHERE ORD_CODART <> '' AND ORD_DESART <> '*** COMMENTO ***'

	UNION

	SELECT
		SUBSTRING(ORD_CODART,1,16) AS ARTICOLO,
		PR.ART_DESCR AS DESARTICOL,
		PR.ART_BARCLI AS BARCODE,
		CAST(ORD_QTAORD AS INT) AS QUANTITA,
		'ORF'+ORF_D.DAGGANCIO AS NUMERO,
		'ORF'+ORF_D.ORD_CODSYS AS RIGA,
		CASE
			WHEN COALESCE(BFO_LEG.DAGGANCIO,'') <> '' THEN 'BFO'+BFO_LEG.DAGGANCIO
			ELSE NULL
		END AS NUMERORIF,
		0 AS PESOKG,
		ORD_UM AS UNIMISURA,
		CAST(ORD_QTAORD AS INT) AS QTACQUISTO
	FROM U_ORF_DD AS ORF_D
	LEFT OUTER JOIN (SELECT DAGGANCIO, ORD_LEGAME, ORD_LEGASY FROM U_BFO_DD) AS BFO_LEG ON BFO_LEG.ORD_LEGASY = ORF_D.ORD_CODSYS AND BFO_LEG.ORD_LEGAME = ORF_D.DAGGANCIO
	LEFT OUTER JOIN U_ART_PR AS PR ON PR.ART_CODPRI = ORF_D.ORD_CODART
	WHERE ORD_CODART <> '' AND ORD_DESART <> '*** COMMENTO ***'
GO

