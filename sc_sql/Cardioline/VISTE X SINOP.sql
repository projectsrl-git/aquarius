	DROP VIEW [dbo].[CAUSMAGA_AQ]
GO

CREATE VIEW [dbo].[CAUSMAGA_AQ]  
AS
	SELECT
		SUBSTRING(CODICE,4,3) AS CODCAUMAG,
		DESCRI AS DESCAUMAG,
		CASE
			WHEN DESCRI LIKE '%INVENTARIO%' THEN
				CASE
					WHEN SUBSTRING(LIBERA,1,1) = 'C' THEN '+'
					WHEN SUBSTRING(LIBERA,1,1) = 'S' THEN '-'
					ELSE 'N'
				END
			ELSE 'N'
		END AS MOVINVENT,
		CASE
			WHEN DESIVA LIKE '%Stato: N%' THEN 'N'
			ELSE 'S'
		END AS STATO,
		'N' AS MOVCADAAQ,
		CASE
			WHEN DESCRI LIKE '%INVENTARIO%' THEN 'N'
			WHEN SUBSTRING(LIBERA,1,1) = 'C' THEN '+'
			ELSE 'N'
		END AS MOVALTRIC,
		'N' AS SCAPERVEN,
		CASE
			WHEN DESCRI LIKE '%INVENTARIO%' THEN 'N'
			WHEN SUBSTRING(LIBERA,1,1) = 'S' THEN '+'
			ELSE 'N'
		END AS ALTRISCAR
	FROM PARA WHERE CODICE LIKE 'TOP%' AND SUBSTRING(LIBERA,42,1) = 'M'
GO

