	FROM U_CLI_AN 
	LEFT OUTER JOIN COMUNI ON COMUNI.CAP = U_CLI_AN.CLI_CAP AND COMUNI.COMUNE = U_CLI_AN.CLI_LOCALI
	LEFT OUTER JOIN PARA AS PARA_STA ON PARA_STA.CODICE = 'STA'+U_CLI_AN.CLI_NAZION

	UNION

	SELECT
		FOR_RAGSOC AS RAGIONESOC, 
		FOR_CODCLI AS CODICE,
		'F' AS TIPO, 
		FOR_INDIR AS INDIR1, 
		NULL AS INDIR2, 
		FOR_CAP AS CAP, 
		FOR_LOCALI AS LOCALITA, 
		COD_FIS AS COD_COMUNE, 
		FOR_LOCALI AS COMUNE,
		FOR_PROVIN AS PROVINCIA, 
		FOR_NAZION AS COD_NAZION,
		PARA_STA.DESCRI AS NAZIONE,
		FOR_PARIVA AS PAIV,
		FOR_CODFIS AS COFI,
		NULL AS PTEL,
		FOR_TELEFO AS TEL, 
		'N' AS RAGGR_SN,
		SUBSTRING(FOR_DATINS,1,4)+SUBSTRING(FOR_DATINS,6,2)+SUBSTRING(FOR_DATINS,9,2) AS ULT_AGG,
		CASE
			WHEN FOR_PARIVA = '' AND FOR_IVACEE = '' THEN 'N'
			ELSE 'S'
		END AS SOGGIVA_SN,
		'N' BLOCCO_SN,
		0 AS PERC_INT,
		NULL AS PERCENT_DA, 
		NULL AS SOLLSCA_SN, 
		NULL ADDINT_SN,
		CASE FOR_VALUTA
			WHEN (SELECT TOP 1 AZI_VALU FROM U_AZI_AN) THEN NULL
			ELSE FOR_VALUTA
		END AS CODVAL,
		CASE FOR_OBSOL
			WHEN 'S' THEN 'N'
			ELSE 'S'
		END AS STATO,
		'' AS NOMEBREVE,
		NULL AS FATTURAREA, 
		CASE LIBERA
			WHEN '' THEN 'N'
			ELSE 'S'
		END AS STATOINTRA,
		NULL AS ALLEGAT_SN,
		'N' AS FTFAX_SN,
		NULL AS FTFAXNUM,
		'N' AS FTMAIL_SN,
		NULL AS FTE_MAIL,
		SUBSTRING(FOR_CONPAG,1,3) AS CODPAG,
		NULL AS PER_FATTUR,
		FOR_EMAIL AS EMAIL
	FROM U_FOR_AN 
	LEFT OUTER JOIN COMUNI ON COMUNI.CAP = U_FOR_AN.FOR_CAP AND COMUNI.COMUNE = U_FOR_AN.FOR_LOCALI
	LEFT OUTER JOIN PARA AS PARA_STA ON PARA_STA.CODICE = 'STA'+U_FOR_AN.FOR_NAZION
