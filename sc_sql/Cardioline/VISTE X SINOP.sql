		'N' AS FTFAX_SN,
		NULL AS FTFAXNUM,
		'N' AS FTMAIL_SN,
		NULL AS FTE_MAIL,
		SUBSTRING(CLI_CONPAG,1,3) AS CODPAG,
		NULL AS PER_FATTUR,
