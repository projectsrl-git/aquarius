		'S' AS STATO,
		SUBSTRING(PAR_DATINS,1,4)+SUBSTRING(PAR_DATINS,6,2)+SUBSTRING(PAR_DATINS,9,2) AS ULT_AGG,
		CASE
			WHEN DESIVA LIKE '%STATOCEE: S%' THEN 'S'
			WHEN DESIVA LIKE '%STATOCEE: N%' THEN 'N'
			ELSE NULL
		END AS STATOCEE
	FROM PARA WHERE CODICE LIKE 'STA%' AND SUBSTRING(CODICE,4,3) <> (SELECT TOP 1 AZI_NAZI FROM U_AZI_AN)
