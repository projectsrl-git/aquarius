CREATE VIEW [dbo].[CLIFO_AQ]  
AS
SELECT * FROM (
	SELECT
		CLI_RAGSOC AS RAGIONESOC, 
		CLI_CODCLI AS CODICE,
		'C' AS TIPO, 
		CLI_INDIR AS INDIR1, 
		NULL AS INDIR2, 
		CLI_CAP AS CAP, 
		CLI_LOCALI AS LOCALITA, 
		COD_FIS AS COD_COMUNE, 
		CLI_LOCALI AS COMUNE,
		CLI_PROVIN AS PROVINCIA, 
		CLI_NAZION AS COD_NAZION,
		PARA_STA.DESCRI AS NAZIONE,
		CLI_PARIVA AS PAIV,
		CLI_CODFIS AS COFI,
		NULL AS PTEL,
		CLI_TELEFO AS TEL, 
		'N' AS RAGGR_SN,
		SUBSTRING(CLI_DATINS,1,4)+SUBSTRING(CLI_DATINS,6,2)+SUBSTRING(CLI_DATINS,9,2) AS ULT_AGG,
		CASE
			WHEN CLI_PARIVA = '' AND CLI_IVACEE = '' THEN 'N'
			ELSE 'S'
		END AS SOGGIVA_SN, 
		CASE
			WHEN CLI_DTCLOK <> '' THEN 'S'
			ELSE 'N'
		END AS BLOCCO_SN,
		0 AS PERC_INT,
		NULL AS PERCENT_DA, 
		NULL AS SOLLSCA_SN, 
		NULL ADDINT_SN,
		CASE CLI_VALUTA
			WHEN (SELECT TOP 1 AZI_VALU FROM U_AZI_AN) THEN NULL
			ELSE CLI_VALUTA
		END AS CODVAL,
		CASE CLI_OBSOL
			WHEN 'S' THEN 'N'
			ELSE 'S'
		END AS STATO,
		CLI_RAGRID AS NOMEBREVE,
		NULL AS FATTURAREA, 
