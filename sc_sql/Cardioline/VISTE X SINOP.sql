CREATE VIEW [dbo].[ANAMAGA_AQ]  
AS
	SELECT
		SUBSTRING(PR.ART_CODPRI,1,16) AS CODICE,
		PR.ART_DESCR AS DESCRIZION,
		PR.ART_DESRID AS CODICERICE,
		PR.ART_CODITG1 AS CODICEVUOT,
		--NULL AS CODICEALTE,
		CASE
			WHEN COALESCE(DIT.DIT_GRUPPO,'') <> '' AND COALESCE(DIS.DIS_CODART,'') = '' THEN 'K'
			WHEN COALESCE(DIT.DIT_GRUPPO,'') <> '' AND COALESCE(DIS.DIS_CODART,'') <> '' THEN 'S'
			ELSE 'M'
		END AS TIPO,
		PR.ART_UNIMIS AS UNITAMIVEN,
		AN.ART_CODGMI AS GRUPPOMERC,
		--CASE
		--	WHEN PR.ART_CGE = '' THEN NULL
		--	ELSE PR.ART_CGE
		--END AS CONTROVEN,
		CASE PR.ART_OBSOL
			WHEN 'S' THEN 'N'
			ELSE 'S'
		END AS STATO
		--NULL AS MARCHIO
	FROM U_ART_PR AS PR
	LEFT OUTER JOIN U_ART_AN AS AN ON AN.ART_CODSOC = PR.ART_CODSOC AND AN.ART_CODPRI = PR.ART_CODPRI
	LEFT OUTER JOIN (SELECT DIT_GRUPPO FROM U_DIS_TT GROUP BY DIT_GRUPPO) AS DIT ON PR.ART_CODPRI = DIT.DIT_GRUPPO
	LEFT OUTER JOIN (SELECT DIS_CODART FROM U_DIS_DD GROUP BY DIS_CODART) AS DIS ON PR.ART_CODPRI = DIS.DIS_CODART
	WHERE LEN(RTRIM(PR.ART_CODPRI)) <= 16
GO

