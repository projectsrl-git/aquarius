GO

CREATE VIEW [dbo].[ORDPROD_AQ]  
AS
	SELECT
		CASE ART_TIPOPR
			WHEN 'I' THEN 'OP'
			ELSE 'OE'
		END AS TIPO,
		CAST(SUBSTRING(DATPRG,1,4) AS DECIMAL(6,0)) AS ANNO, 
		CAST(NUMPRG AS DECIMAL(10,0)) AS NUM,
		STATO,
		--CAST(0 AS DECIMAL(6,0)) AS ANNOF,
		--CAST(0 AS DECIMAL(10,0)) AS NUMF,
		SUBSTRING(COMP1,1,16) AS CODPADRE,
		CAST(COALESCE(QTA4,QTA3,QTA2) AS FLOAT) AS Q1,
		COALESCE(SUBSTRING(COMP4,1,16),SUBSTRING(COMP3,1,16),SUBSTRING(COMP2,1,16)) AS CODART,
		IDPRG,
		Q2
	FROM (
		SELECT * FROM (
			SELECT P1.IDPRG, PR.ART_TIPOPR, P1.COMP AS COMP1, P1.NUMPRG, P1.DATPRG, P2.COMP AS COMP2, P2.QTA AS QTA2, D2.DIS_FITTIZ AS F2, P3.COMP AS COMP3, P3.QTA AS QTA3, D3.DIS_FITTIZ AS F3, P4.COMP AS COMP4, P4.QTA AS QTA4, D4.DIS_FITTIZ AS F4, CASE WHEN PA.QTAPROD >= PA.QTA THEN 'S' ELSE '' END AS STATO, NULL AS Q2
			FROM PRODUZIONE AS P1
			LEFT OUTER JOIN PRODUZIONE AS P2 ON P2.IDPRG = P1.IDPRG AND P2.PARENT = P1.CHIAVE AND P2.FASELA = ''
			LEFT OUTER JOIN U_DIS_DD AS D2 ON D2.DIS_GRUPPO = P1.COMP AND D2.DIS_CODART = P2.COMP
			LEFT OUTER JOIN PRODUZIONE AS P3 ON P3.IDPRG = P1.IDPRG AND P3.PARENT = P2.CHIAVE AND P3.FASELA = '' AND D2.DIS_FITTIZ = 1
			LEFT OUTER JOIN U_DIS_DD AS D3 ON D3.DIS_GRUPPO = P2.COMP AND D3.DIS_CODART = P3.COMP
			LEFT OUTER JOIN PRODUZIONE AS P4 ON P4.IDPRG = P1.IDPRG AND P4.PARENT = P3.CHIAVE AND P4.FASELA = '' AND D3.DIS_FITTIZ = 1
			LEFT OUTER JOIN U_DIS_DD AS D4 ON D4.DIS_GRUPPO = P3.COMP AND D4.DIS_CODART = P4.COMP
			LEFT OUTER JOIN U_ART_PR AS PR ON PR.ART_CODPRI = P1.COMP
			LEFT OUTER JOIN (SELECT IDPRG, COMP, SUM(QTA) AS QTA, SUM(QTAPROD) AS QTAPROD FROM PROD_AVANZA GROUP BY IDPRG, COMP) AS PA ON PA.IDPRG = P1.IDPRG AND PA.COMP = P1.COMP
			WHERE P1.PARENT = '000000001_' AND NOT (D2.DIS_FITTIZ = 1 AND D3.DIS_FITTIZ = 1)

			UNION

			SELECT P1.IDPRG, PR.ART_TIPOPR, P1.COMP AS COMP1, P1.NUMPRG, P1.DATPRG, PP.COMP AS COMP2, PP.QTA AS QTA2, CAST(1 AS BIT) AS F2, NULL AS COMP3, NULL AS QTA3, NULL AS F3, NULL AS COMP4, NULL AS QTA4, NULL AS F4, CASE WHEN PC.NROWS > 0 THEN 'M' ELSE '' END AS STATO, PC.QTAPREL AS Q2
			FROM PRODUZIONE AS P1
			LEFT OUTER JOIN PRODUZIONE AS PP ON PP.IDPRG = P1.IDPRG AND PP.CHIAVE = P1.CHIAVE
			LEFT OUTER JOIN U_ART_PR AS PR ON PR.ART_CODPRI = P1.COMP
			LEFT OUTER JOIN (SELECT IDPRG, CODART, COUNT(*) AS NROWS, SUM(1) AS QTAPREL, SUM(CASE QTAPROD WHEN 0 THEN 1 ELSE 0 END) AS QTAPREL_NON_VERS FROM PROD_CARICHI GROUP BY IDPRG, CODART) AS PC ON PC.IDPRG = P1.IDPRG AND PC.CODART = P1.COMP
			WHERE P1.PARENT = '000000001_'

		) AS T_MAIN
	) AS MAIN
GO

