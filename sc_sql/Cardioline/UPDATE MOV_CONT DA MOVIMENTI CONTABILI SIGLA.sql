CREATE VIEW [dbo].[CARD_MOVCO_MOVIVA]  
AS

SELECT CARD_MOVCO.ESEREGISTR,CARD_MOVCO.NUMERO,CARD_MOVCO.RIGA,CARD_MOVCO.SOTTOCONTO,CARD_MOVCO.EIMPORTO,CARD_MOVCO.SEGNO,CARD_MOVIVA.CODIVA,CARD_MOVIVA.IMPOSTA,
	COALESCE(
		CASE 
			WHEN CARD_MOVIVA.CODIVA  	= '20BL'
				THEN '20L'

			WHEN CARD_MOVIVA.CODIVA  	= '20BS'
				THEN '20S'

			WHEN CARD_MOVIVA.CODIVA  	= '20IN'
				THEN '20N'		
		
			WHEN CARD_MOVIVA.CODIVA  	= '20RC'
				THEN '20C'				
		
			WHEN CARD_MOVIVA.CODIVA  	= '20SE'
				THEN '20E'				

			WHEN CARD_MOVIVA.CODIVA  	= '21BL'
				THEN '21L'

			WHEN CARD_MOVIVA.CODIVA  	= '21BS'
				THEN '21S'

			WHEN CARD_MOVIVA.CODIVA  	= '21IN'
				THEN '21N'		
		
			WHEN CARD_MOVIVA.CODIVA  	= '21RC'
				THEN '21C'				
		
			WHEN CARD_MOVIVA.CODIVA  	= '21SE'
				THEN '21E'				

			WHEN CARD_MOVIVA.CODIVA  	= 'NI 1'
				THEN 'NI1'				

			WHEN CARD_MOVIVA.CODIVA  	= 'NI 2'
				THEN 'NI2'		

			WHEN CARD_MOVIVA.CODIVA  	= 'NI 3'
				THEN 'NI3'		

			WHEN CARD_MOVIVA.CODIVA  	= '2017'
				THEN '201'		

			WHEN CARD_MOVIVA.CODIVA  	= '2180'
				THEN '218'
		
			WHEN CARD_MOVIVA.CODIVA  	= '2280'
				THEN '228'
		
			WHEN CARD_MOVIVA.CODIVA  	= '21AR'
				THEN '21R'
		
			WHEN CARD_MOVIVA.CODIVA  	= '22AR'
				THEN '22R'
	
			ELSE

				CASE 
					WHEN LEN(CARD_MOVIVA.CODIVA) > 3
						THEN SUBSTRING(CARD_MOVIVA.CODIVA,1,1) + SUBSTRING(CARD_MOVIVA.CODIVA,3,2) 	-- TOGLIE IL SECONDO CARATTERE
					ELSE CARD_MOVIVA.CODIVA
				END
		END 
	,'') AS NEW_CODIVA,
	CARD_MOVCO.CODPAGAM,
	COALESCE(
	CASE 

		WHEN CARD_MOVCO.CODPAGAM  	= '004'
			THEN '304'

		WHEN CARD_MOVCO.CODPAGAM  	= '97'
			THEN '397'

		WHEN CARD_MOVCO.CODPAGAM  	= '100'
			THEN '301'

		WHEN CARD_MOVCO.CODPAGAM  	= '175'
			THEN '375'

		WHEN CARD_MOVCO.CODPAGAM  	= '176'
			THEN '376'

		WHEN CARD_MOVCO.CODPAGAM  	= '204'
			THEN '324'

		WHEN CARD_MOVCO.CODPAGAM  	= 'C101'
			THEN 'C17'

		WHEN CARD_MOVCO.CODPAGAM  	= 'C102'
			THEN 'C18'

		WHEN CARD_MOVCO.CODPAGAM  	= 'C103'
			THEN 'C19'

		WHEN CARD_MOVCO.CODPAGAM  	= 'C104'
			THEN 'C20'

		WHEN CARD_MOVCO.CODPAGAM  	= 'C105'
			THEN 'C21'

		WHEN CARD_MOVCO.CODPAGAM  	= 'V101'
			THEN 'V23'

		WHEN CARD_MOVCO.CODPAGAM  	= 'V102'
			THEN 'V24'

		WHEN CARD_MOVCO.CODPAGAM  	= 'V103'
			THEN 'V25'

		WHEN CARD_MOVCO.CODPAGAM  	= 'V104'
			THEN 'V26'

		WHEN CARD_MOVCO.CODPAGAM  	= 'V105'
			THEN 'V27'

		WHEN CARD_MOVCO.CODPAGAM  	= 'V201'
			THEN 'V28'

		WHEN CARD_MOVCO.CODPAGAM  	= 'V202'
			THEN 'V29'

		WHEN CARD_MOVCO.CODPAGAM  	= 'V203'
			THEN 'V30'

		WHEN CARD_MOVCO.CODPAGAM  	= 'V208'
			THEN 'V31'

		WHEN CARD_MOVCO.CODPAGAM  	= 'V211'
			THEN 'V32'

		WHEN CARD_MOVCO.CODPAGAM  	= 'V212'
			THEN 'V33'

		WHEN CARD_MOVCO.CODPAGAM  	= 'V213'
			THEN 'V34'

		WHEN CARD_MOVCO.CODPAGAM  	= 'V214'
			THEN 'V35'

		WHEN CARD_MOVCO.CODPAGAM  	= 'V306'
			THEN 'V36'

		WHEN CARD_MOVCO.CODPAGAM  	= 'V309'
			THEN 'V37'

		WHEN CARD_MOVCO.CODPAGAM  	= 'V310'
			THEN 'V38'

	ELSE

		CASE WHEN LEN(CARD_MOVCO.CODPAGAM) > 3 THEN

			CASE WHEN SUBSTRING(CARD_MOVCO.CODPAGAM,1,1) = '0' THEN

				CASE WHEN SUBSTRING(CARD_MOVCO.CODPAGAM,2,1) <> '0' THEN SUBSTRING(CARD_MOVCO.CODPAGAM,2,3) 		-- TOGLIE IL PRIMO CARATTERE	
				ELSE SUBSTRING(CARD_MOVCO.CODPAGAM,1,1) + SUBSTRING(CARD_MOVCO.CODPAGAM,3,2) 					-- TOGLIE IL SECONDO CARATTERE
				END
		
			ELSE
				SUBSTRING(CARD_MOVCO.CODPAGAM,1,1) + SUBSTRING(CARD_MOVCO.CODPAGAM,3,2) 		-- TOGLIE IL SECONDO CARATTERE
			END

		END

	END 
	,'') AS NEW_CODPAGAM,
	CARD_MOVCO.MOV_IVA_SN,
	CARD_MOVCO.CODICEIVA,
	COALESCE(
		CASE 
			WHEN CARD_MOVCO.CODICEIVA  	= '20BL'
				THEN '20L'

			WHEN CARD_MOVCO.CODICEIVA  	= '20BS'
				THEN '20S'

			WHEN CARD_MOVCO.CODICEIVA  	= '20IN'
				THEN '20N'		
		
			WHEN CARD_MOVCO.CODICEIVA  	= '20RC'
				THEN '20C'				
		
			WHEN CARD_MOVCO.CODICEIVA  	= '20SE'
				THEN '20E'				

			WHEN CARD_MOVCO.CODICEIVA  	= '21BL'
				THEN '21L'

			WHEN CARD_MOVCO.CODICEIVA  	= '21BS'
				THEN '21S'

			WHEN CARD_MOVCO.CODICEIVA  	= '21IN'
				THEN '21N'		
		
			WHEN CARD_MOVCO.CODICEIVA  	= '21RC'
				THEN '21C'				
		
			WHEN CARD_MOVCO.CODICEIVA  	= '21SE'
				THEN '21E'				

			WHEN CARD_MOVCO.CODICEIVA  	= 'NI 1'
				THEN 'NI1'				

			WHEN CARD_MOVCO.CODICEIVA  	= 'NI 2'
				THEN 'NI2'		

			WHEN CARD_MOVCO.CODICEIVA  	= 'NI 3'
				THEN 'NI3'		

			WHEN CARD_MOVCO.CODICEIVA  	= '2017'
				THEN '201'		

			WHEN CARD_MOVCO.CODICEIVA  	= '2180'
				THEN '218'
		
			WHEN CARD_MOVCO.CODICEIVA  	= '2280'
				THEN '228'
		
			WHEN CARD_MOVCO.CODICEIVA  	= '21AR'
				THEN '21R'
		
			WHEN CARD_MOVCO.CODICEIVA  	= '22AR'
				THEN '22R'
	
			ELSE

				CASE 
					WHEN LEN(CARD_MOVCO.CODICEIVA) > 3
						THEN SUBSTRING(CARD_MOVCO.CODICEIVA,1,1) + SUBSTRING(CARD_MOVCO.CODICEIVA,3,2) 	-- TOGLIE IL SECONDO CARATTERE
					ELSE CARD_MOVCO.CODICEIVA
				END
		END 
	,'') AS NEW_CODICEIVA
FROM CARD_MOVCO 
LEFT OUTER JOIN CARD_MOVIVA ON CARD_MOVIVA.NUMERO = CARD_MOVCO.NUMERO AND '00'+CARD_MOVIVA.RIGA = CARD_MOVCO.RIGA
--WHERE CARD_MOVCO.NUMERO = '0037696'
--ORDER BY CARD_MOVCO.ESEREGISTR,CARD_MOVCO.NUMERO,CARD_MOVCO.RIGA

GO


