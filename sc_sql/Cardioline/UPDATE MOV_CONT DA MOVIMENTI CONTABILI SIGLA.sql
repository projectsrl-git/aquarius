UPDATE MOV_CONT SET
	MOV_MODPAG = COALESCE((SELECT TOP 1 NEW_CODPAGAM FROM CARD_MOVCO_MOVIVA WHERE ESEREGISTR = MOV_CONT.MOV_ANNO AND CAST(NUMERO AS NUMERIC (10,0)) = CAST(MOV_CONT.MOV_NREGIS AS NUMERIC(10,0)) ORDER BY RIGA), MOV_CONT.MOV_MODPAG),
	MOV_IVA = 
		CASE
			WHEN LEN(MOV_CONTO) >= 13 THEN MOV_IVA
			WHEN COALESCE((SELECT CON_CONTO FROM CONTI WHERE CON_ANNO = MOV_CONT.MOV_ANNO AND CON_CONTO = MOV_CONT.MOV_CONTO AND con_tipoco='I'),'') <> '' THEN MOV_IVA
			ELSE
				CASE
					WHEN COALESCE((SELECT TOP 1 NEW_CODICEIVA FROM CARD_MOVCO_MOVIVA WHERE COALESCE(CODICEIVA,'') <> '' AND ESEREGISTR = MOV_CONT.MOV_ANNO AND CAST(NUMERO AS NUMERIC (10,0)) = CAST(MOV_CONT.MOV_NREGIS AS NUMERIC(10,0))),'') <> ''
					THEN 
						COALESCE((SELECT TOP 1 NEW_CODICEIVA FROM CARD_MOVCO_MOVIVA WHERE COALESCE(CODICEIVA,'') <> '' AND ESEREGISTR = MOV_CONT.MOV_ANNO AND CAST(NUMERO AS NUMERIC (10,0)) = CAST(MOV_CONT.MOV_NREGIS AS NUMERIC(10,0)) AND EIMPORTO = CAST(MOV_CONT.MOV_IMP AS float) AND SOTTOCONTO = MOV_CONT.MOV_CONTO), MOV_CONT.MOV_IVA)
					ELSE
						COALESCE((SELECT TOP 1 NEW_CODIVA FROM CARD_MOVCO_MOVIVA WHERE NEW_CODIVA <> '' AND ESEREGISTR = MOV_CONT.MOV_ANNO AND CAST(NUMERO AS NUMERIC (10,0)) = CAST(MOV_CONT.MOV_NREGIS AS NUMERIC(10,0)) AND EIMPORTO = CAST(MOV_CONT.MOV_IMP AS float) AND SOTTOCONTO = MOV_CONT.MOV_CONTO),
							COALESCE((SELECT TOP 1 NEW_CODIVA FROM CARD_MOVCO_MOVIVA WHERE NEW_CODIVA <> '' AND ESEREGISTR = MOV_CONT.MOV_ANNO AND CAST(NUMERO AS NUMERIC (10,0)) = CAST(MOV_CONT.MOV_NREGIS AS NUMERIC(10,0)) ORDER BY RIGA), MOV_CONT.MOV_IVA))	
				END
		END

WHERE MOV_ANNO <= '2014' AND MOV_DTREG <= '2014/12/31' --AND MOV_NUMPRO = '_4990EUADA'
--WHERE MOV_NUMPRO = '_49G15FLMQ' 


UPDATE INDICE SET
	MOV_MODPAG = COALESCE((SELECT TOP 1 NEW_CODPAGAM FROM CARD_MOVCO_MOVIVA WHERE ESEREGISTR = INDICE.MOV_ANNO AND CAST(NUMERO AS NUMERIC (10,0)) = CAST(INDICE.MOV_NREGIS AS NUMERIC(10,0)) ORDER BY RIGA), INDICE.MOV_MODPAG)
