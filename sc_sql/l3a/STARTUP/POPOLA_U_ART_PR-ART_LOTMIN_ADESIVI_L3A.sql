UPDATE U_ART_PR SET ART_LOTMIN = COALESCE((SELECT TOP 1 ORD_LOTMIN FROM U_ORF_DD WHERE ORD_LOTMIN > 0 AND ORD_CODART = U_ART_PR.ART_CODPRI ORDER BY ORS_DATORD DESC),0)
WHERE (ART_CODMAT = '004' OR ART_CODMAT = '014') AND COALESCE(ART_LOTMIN,0) = 0

SELECT
	ART.ART_CODPRI,
	ART.ART_DESCR,
	ART.ART_LOTMIN
FROM U_ART_PR AS ART
WHERE (ART.ART_CODMAT = '004' OR ART.ART_CODMAT = '014')
ORDER BY ART.ART_CODPRI