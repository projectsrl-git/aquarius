IF  EXISTS (SELECT * FROM sys.views WHERE object_id = OBJECT_ID(N'[dbo].[SCADENZE_VALORE_CONSEGNATO_DA_CONSEGNARE]'))
	DROP VIEW [dbo].[SCADENZE_VALORE_CONSEGNATO_DA_CONSEGNARE]
GO

CREATE VIEW [dbo].[SCADENZE_VALORE_CONSEGNATO_DA_CONSEGNARE]
AS

SELECT
	FORNITORE,
	RAG_SOCIALE,
	SUM(CONSEGNA_6_MESI_FA) AS CONSEGNA_6_MESI_FA,
	SUM(DA_PAGARE_6_MESI_FA) AS DA_PAGARE_6_MESI_FA,
	SUM(CONSEGNA_5_MESI_FA) AS CONSEGNA_5_MESI_FA,
	SUM(DA_PAGARE_5_MESI_FA) AS DA_PAGARE_5_MESI_FA,
	SUM(CONSEGNA_4_MESI_FA) AS CONSEGNA_4_MESI_FA,
	SUM(DA_PAGARE_4_MESI_FA) AS DA_PAGARE_4_MESI_FA,
	SUM(CONSEGNA_3_MESI_FA) AS CONSEGNA_3_MESI_FA,
	SUM(DA_PAGARE_3_MESI_FA) AS DA_PAGARE_3_MESI_FA,
	SUM(CONSEGNA_2_MESI_FA) AS CONSEGNA_2_MESI_FA,
	SUM(DA_PAGARE_2_MESI_FA) AS DA_PAGARE_2_MESI_FA,
	SUM(CONSEGNA_1_MESE_FA) AS CONSEGNA_1_MESE_FA,
	SUM(DA_PAGARE_1_MESE_FA) AS DA_PAGARE_1_MESE_FA,
	SUM(CONSEGNA_MESE_ATTUALE) AS CONSEGNA_MESE_ATTUALE,
	SUM(DA_PAGARE_MESE_ATTUALE) AS DA_PAGARE_MESE_ATTUALE,
	SUM(CONSEGNA_FRA_1_MESE) AS CONSEGNA_FRA_1_MESE,
	SUM(DA_PAGARE_FRA_1_MESE) AS DA_PAGARE_FRA_1_MESE,
	SUM(CONSEGNA_FRA_2_MESI) AS CONSEGNA_FRA_2_MESI,
	SUM(DA_PAGARE_FRA_2_MESI) AS DA_PAGARE_FRA_2_MESI,
	SUM(CONSEGNA_FRA_3_MESI) AS CONSEGNA_FRA_3_MESI,
	SUM(DA_PAGARE_FRA_3_MESI) AS DA_PAGARE_FRA_3_MESI,
	SUM(CONSEGNA_FRA_4_MESI) AS CONSEGNA_FRA_4_MESI,
	SUM(DA_PAGARE_FRA_4_MESI) AS DA_PAGARE_FRA_4_MESI,
	SUM(CONSEGNA_FRA_5_MESI) AS CONSEGNA_FRA_5_MESI,
	SUM(DA_PAGARE_FRA_5_MESI) AS DA_PAGARE_FRA_5_MESI
FROM (
	SELECT
		ORD_CODCLI AS FORNITORE,
		ORD_RAGSOC AS RAG_SOCIALE,
		CASE WHEN MESE = (REPLACE(SUBSTRING(CONVERT(VARCHAR(19), DATEADD(month, -6, GETDATE()), 120),1,7),'-','/')) THEN VALORE ELSE 0 END AS CONSEGNA_6_MESI_FA,
		CASE WHEN MESE = (REPLACE(SUBSTRING(CONVERT(VARCHAR(19), DATEADD(month, -6, GETDATE()), 120),1,7),'-','/')) THEN DA_PAGARE ELSE 0 END AS DA_PAGARE_6_MESI_FA,
		CASE WHEN MESE = (REPLACE(SUBSTRING(CONVERT(VARCHAR(19), DATEADD(month, -5, GETDATE()), 120),1,7),'-','/')) THEN VALORE ELSE 0 END AS CONSEGNA_5_MESI_FA,
		CASE WHEN MESE = (REPLACE(SUBSTRING(CONVERT(VARCHAR(19), DATEADD(month, -5, GETDATE()), 120),1,7),'-','/')) THEN DA_PAGARE ELSE 0 END AS DA_PAGARE_5_MESI_FA,
		CASE WHEN MESE = (REPLACE(SUBSTRING(CONVERT(VARCHAR(19), DATEADD(month, -4, GETDATE()), 120),1,7),'-','/')) THEN VALORE ELSE 0 END AS CONSEGNA_4_MESI_FA,
		CASE WHEN MESE = (REPLACE(SUBSTRING(CONVERT(VARCHAR(19), DATEADD(month, -4, GETDATE()), 120),1,7),'-','/')) THEN DA_PAGARE ELSE 0 END AS DA_PAGARE_4_MESI_FA,
		CASE WHEN MESE = (REPLACE(SUBSTRING(CONVERT(VARCHAR(19), DATEADD(month, -3, GETDATE()), 120),1,7),'-','/')) THEN VALORE ELSE 0 END AS CONSEGNA_3_MESI_FA,
		CASE WHEN MESE = (REPLACE(SUBSTRING(CONVERT(VARCHAR(19), DATEADD(month, -3, GETDATE()), 120),1,7),'-','/')) THEN DA_PAGARE ELSE 0 END AS DA_PAGARE_3_MESI_FA,
		CASE WHEN MESE = (REPLACE(SUBSTRING(CONVERT(VARCHAR(19), DATEADD(month, -2, GETDATE()), 120),1,7),'-','/')) THEN VALORE ELSE 0 END AS CONSEGNA_2_MESI_FA,
		CASE WHEN MESE = (REPLACE(SUBSTRING(CONVERT(VARCHAR(19), DATEADD(month, -2, GETDATE()), 120),1,7),'-','/')) THEN DA_PAGARE ELSE 0 END AS DA_PAGARE_2_MESI_FA,
		CASE WHEN MESE = (REPLACE(SUBSTRING(CONVERT(VARCHAR(19), DATEADD(month, -1, GETDATE()), 120),1,7),'-','/')) THEN VALORE ELSE 0 END AS CONSEGNA_1_MESE_FA,
		CASE WHEN MESE = (REPLACE(SUBSTRING(CONVERT(VARCHAR(19), DATEADD(month, -1, GETDATE()), 120),1,7),'-','/')) THEN DA_PAGARE ELSE 0 END AS DA_PAGARE_1_MESE_FA,
		CASE WHEN MESE = (REPLACE(SUBSTRING(CONVERT(VARCHAR(19), GETDATE(), 120),1,7),'-','/')) THEN VALORE ELSE 0 END AS CONSEGNA_MESE_ATTUALE,
		CASE WHEN MESE = (REPLACE(SUBSTRING(CONVERT(VARCHAR(19), GETDATE(), 120),1,7),'-','/')) THEN DA_PAGARE ELSE 0 END AS DA_PAGARE_MESE_ATTUALE,
		CASE WHEN MESE = (REPLACE(SUBSTRING(CONVERT(VARCHAR(19), DATEADD(month, 1, GETDATE()), 120),1,7),'-','/')) THEN VALORE ELSE 0 END AS CONSEGNA_FRA_1_MESE,
		CASE WHEN MESE = (REPLACE(SUBSTRING(CONVERT(VARCHAR(19), DATEADD(month, 1, GETDATE()), 120),1,7),'-','/')) THEN DA_PAGARE ELSE 0 END AS DA_PAGARE_FRA_1_MESE,
		CASE WHEN MESE = (REPLACE(SUBSTRING(CONVERT(VARCHAR(19), DATEADD(month, 2, GETDATE()), 120),1,7),'-','/')) THEN VALORE ELSE 0 END AS CONSEGNA_FRA_2_MESI,
		CASE WHEN MESE = (REPLACE(SUBSTRING(CONVERT(VARCHAR(19), DATEADD(month, 2, GETDATE()), 120),1,7),'-','/')) THEN DA_PAGARE ELSE 0 END AS DA_PAGARE_FRA_2_MESI,
		CASE WHEN MESE = (REPLACE(SUBSTRING(CONVERT(VARCHAR(19), DATEADD(month, 3, GETDATE()), 120),1,7),'-','/')) THEN VALORE ELSE 0 END AS CONSEGNA_FRA_3_MESI,
		CASE WHEN MESE = (REPLACE(SUBSTRING(CONVERT(VARCHAR(19), DATEADD(month, 3, GETDATE()), 120),1,7),'-','/')) THEN DA_PAGARE ELSE 0 END AS DA_PAGARE_FRA_3_MESI,
		CASE WHEN MESE = (REPLACE(SUBSTRING(CONVERT(VARCHAR(19), DATEADD(month, 4, GETDATE()), 120),1,7),'-','/')) THEN VALORE ELSE 0 END AS CONSEGNA_FRA_4_MESI,
		CASE WHEN MESE = (REPLACE(SUBSTRING(CONVERT(VARCHAR(19), DATEADD(month, 4, GETDATE()), 120),1,7),'-','/')) THEN DA_PAGARE ELSE 0 END AS DA_PAGARE_FRA_4_MESI,
		CASE WHEN MESE = (REPLACE(SUBSTRING(CONVERT(VARCHAR(19), DATEADD(month, 5, GETDATE()), 120),1,7),'-','/')) THEN VALORE ELSE 0 END AS CONSEGNA_FRA_5_MESI,
		CASE WHEN MESE = (REPLACE(SUBSTRING(CONVERT(VARCHAR(19), DATEADD(month, 5, GETDATE()), 120),1,7),'-','/')) THEN DA_PAGARE ELSE 0 END AS DA_PAGARE_FRA_5_MESI
	FROM (

		SELECT ORD_CODCLI,ORD_RAGSOC,SUM(VALORE) AS VALORE,SUBSTRING(ORD_DATCON,1,7) AS MESE, SUM(DA_PAGARE) AS DA_PAGARE FROM (
			SELECT TT.ORD_CODCLI, TT.ORD_RAGSOC, (DD.ORD_QTAORD - DD.ORD_QTAEV) * DD.ORD_PRZNET AS VALORE, DD.ORD_DATCON, CAST(0 AS NUMERIC(19,2)) AS DA_PAGARE FROM U_ORF_DD AS DD
			RIGHT OUTER JOIN U_ORF_TT AS TT ON DD.DAGGANCIO = TT.TAGGANCIO
			WHERE TT.ORD_CHIUSO = 0 AND DD.ORD_CHIUSO = 0 AND DD.ORD_QTAORD > DD.ORD_QTAEV AND LEN(RTRIM(LTRIM(DD.ORD_DATCON))) = 10 AND SUBSTRING(ORD_DATCON,1,7) >= (REPLACE(SUBSTRING(CONVERT(VARCHAR(19), DATEADD(month, -6, GETDATE()), 120),1,7),'-','/'))
			 AND SUBSTRING(ORD_DATCON,1,7) <= (REPLACE(SUBSTRING(CONVERT(VARCHAR(19), DATEADD(month, 5, GETDATE()), 120),1,7),'-','/'))

			UNION

			SELECT TT.ORD_CODCLI, TT.ORD_RAGSOC, DD.ORD_QTAORD * DD.ORD_PRZNET AS VALORE, TT.ORD_DATORD AS ORD_DATCON, CAST(0 AS NUMERIC(19,2)) AS DA_PAGARE FROM U_BFO_DD AS DD
			RIGHT OUTER JOIN U_BFO_TT AS TT ON DD.DAGGANCIO = TT.TAGGANCIO AND LEN(RTRIM(LTRIM(TT.ORD_DATORD))) = 10 AND SUBSTRING(TT.ORD_DATORD,1,7) >= (REPLACE(SUBSTRING(CONVERT(VARCHAR(19), DATEADD(month, -6, GETDATE()), 120),1,7),'-','/'))
			 AND SUBSTRING(TT.ORD_DATORD,1,7) <= (REPLACE(SUBSTRING(CONVERT(VARCHAR(19), DATEADD(month, 5, GETDATE()), 120),1,7),'-','/'))

			UNION

			SELECT 
				SUBSTRING(MAIN.PAR_CODCLI,5,10) AS ORD_CODCLI,
				(SELECT COALESCE(FOR_RAGSOC,'') AS FOR_RAGSOC FROM U_FOR_AN WHERE FOR_CODCLI = SUBSTRING(MAIN.PAR_CODCLI,5,10)) AS ORD_RAGSOC,
				CAST(0 AS NUMERIC(19,2)) AS VALORE,
				(SELECT TOP 1 PAR_DTSCAD FROM PART_FOR AS PF WHERE PF.PAR_NPROG <> 100 AND PF.PAR_TIPODO = 'FAT' AND PF.PAR_NPART = MAIN.PAR_NPART ORDER BY PAR_DTSCAD DESC) AS ORD_DATCON,
				-1*SALDO AS DA_PAGARE
			FROM (
				SELECT PAR_CODCLI, PAR_NPART, SUM(CASE WHEN PAR_TMOV = 'D' THEN PAR_FATT ELSE -1 * PAR_FATT END) AS SALDO FROM PART_FOR WHERE PAR_NPROG <> 100 AND PAR_NPART IN
						(SELECT PAR_NPART FROM PART_FOR WHERE PAR_TIPODO = 'FAT' 
							--AND PAR_CODPAG IN (SELECT SUBSTRING(CODICE,4,3) FROM PARA WHERE CODICE LIKE 'CPA%' AND SUBSTRING(LIBERA,10,1) = 'X')
							AND LEN(RTRIM(LTRIM(PAR_DTSCAD))) = 10 AND SUBSTRING(PAR_DTSCAD,1,7) >= (REPLACE(SUBSTRING(CONVERT(VARCHAR(19), DATEADD(month, -6, GETDATE()), 120),1,7),'-','/'))
											AND SUBSTRING(PAR_DTSCAD,1,7) <= (REPLACE(SUBSTRING(CONVERT(VARCHAR(19), DATEADD(month, 5, GETDATE()), 120),1,7),'-','/'))
						) GROUP BY PAR_CODCLI,PAR_NPART
					) AS MAIN
			WHERE MAIN.SALDO <> 0

			) AS MAIN1
		WHERE VALORE > 0 OR DA_PAGARE <> 0
		GROUP BY ORD_CODCLI,ORD_RAGSOC,ORD_DATCON
		--ORDER BY ORD_RAGSOC,MESE
		) AS MAIN2
	) AS MAIN3
GROUP BY FORNITORE,RAG_SOCIALE

GO

