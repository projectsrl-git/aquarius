SELECT 
	ORD_NUMORD,MOV_NPROT,MOV_NDOC,MOV_DTREG,MOV_NPART , MOV_NREGIS
FROM MOV_CONT AS MOV
LEFT OUTER JOIN U_FAT_TT AS FAT
ON MOV_NDOC = ORD_NPROT
WHERE MOV_ANNO = '2015' AND MOV_TOP IN ('FVS','NCS') AND ORD_NUMORD>='800000'
ORDER BY ORD_NUMORD 

SELECT 
	ORD_NUMORD,MOV_NPROT,MOV_NDOC,MOV_DTREG,MOV_NPART , MOV_NREGIS
FROM INDICE AS MOV
LEFT OUTER JOIN U_FAT_TT AS FAT
ON MOV_NDOC = ORD_NPROT
WHERE MOV_ANNO = '2015' AND MOV_TOP IN ('FVS','NCS') AND ORD_NUMORD>='800000'
ORDER BY ORD_NUMORD 

UPDATE MOV_CONT
SET MOV_NPROT = (
SELECT 
	ORD_NUMORD 
FROM U_FAT_TT WHERE MOV_NDOC = ORD_NPROT AND ORD_NUMORD>='800000'
)
WHERE MOV_ANNO = '2015' AND MOV_TOP IN ('FVS','NCS') 


UPDATE INDICE
SET MOV_NPROT = (
SELECT 
	ORD_NUMORD 
FROM U_FAT_TT WHERE MOV_NDOC = ORD_NPROT AND ORD_NUMORD>='800000'
)
WHERE MOV_ANNO = '2015' AND MOV_TOP IN ('FVS','NCS') 