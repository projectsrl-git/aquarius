

WITH AlberoPROD
AS (
	SELECT 
		IDNODE,
		0 AS LIVELLO,
		PARENT AS PADRE,
		CAST(COALESCE(RTRIM(COMP),'') AS VARCHAR(1000)) AS GERARCHIA,
		CAST(COALESCE(RTRIM(CHIAVE),'') AS VARCHAR(1000)) AS GERARC_KEY,
		CAST(1 AS NUMERIC(10,2)) AS DIS_QTA,
		CAST(1 AS NUMERIC(10,2)) AS DIS_QTATOT,
		PARENT,
		CHIAVE,
		IDPRG,
		NUMPRG,
		DATPRG,
		COMP,
		DESCOMP,
		DIS_FITTIZ,
		CHIAVE AS MPARENT
	FROM PRODUZIONE
	WHERE COMP <> '' and PARENT = '000000001_' AND IDPRG NOT IN (SELECT IDPRG FROM PRODUZIONE WHERE FLGCLOSED = 1)
	UNION ALL
	SELECT 
		PR.IDNODE,
		AlberoPROD.LIVELLO + 1,
		PR.PARENT AS PADRE,
		CAST(AlberoPROD.GERARCHIA+' > '+RTRIM(PR.COMP) AS VARCHAR(1000)) AS GERARCHIA,
		CAST(AlberoPROD.GERARC_KEY+' > '+RTRIM(PR.CHIAVE) AS VARCHAR(1000)) AS GERARCHIA,
		CAST(PR.DIS_QTA AS NUMERIC(10,2)) AS DIS_QTA,
		CAST(PR.DIS_QTA * AlberoPROD.DIS_QTATOT AS NUMERIC(10,2)) AS DIS_QTATOT,
		PR.PARENT,
		PR.CHIAVE,
		PR.IDPRG,
		PR.NUMPRG,
		PR.DATPRG,
		PR.COMP,
		PR.DESCOMP,
		PR.DIS_FITTIZ,
		AlberoPROD.MPARENT AS MPARENT
	FROM PRODUZIONE AS PR
	INNER JOIN AlberoPROD
	--ON PR.IDPRG = AlberoPROD.IDPRG AND PR.PARENT = AlberoPROD.CHIAVE AND PR.DIS_QTA > 0 AND FASELA = ''
	ON PR.IDPRG = AlberoPROD.IDPRG AND PR.PARENT = AlberoPROD.CHIAVE
)

UPDATE PRODUZIONE SET
	LIVELLO = COALESCE((SELECT LIVELLO FROM AlberoPROD WHERE IDNODE = PRODUZIONE.IDNODE),0),
	DIS_QTAT = COALESCE((SELECT DIS_QTATOT FROM AlberoPROD WHERE IDNODE = PRODUZIONE.IDNODE),0),
	MPARENT = COALESCE((SELECT MPARENT FROM AlberoPROD WHERE IDNODE = PRODUZIONE.IDNODE),''),
	GRUART = COALESCE((SELECT GRUART FROM (
											SELECT 
												P1.*,
												CASE
													WHEN COALESCE((SELECT TOP 1 COMP FROM PRODUZIONE WHERE IDPRG = P1.IDPRG AND PARENT = P1.CHIAVE AND DIS_QTA > 0),'') <> '' THEN 'G'
													ELSE 'A'
												END AS GRUART
											FROM AlberoPROD AS P1
										) AS MAIN
										WHERE IDNODE = PRODUZIONE.IDNODE)
						,''),
	GERARCHIA = COALESCE((SELECT GERARCHIA FROM AlberoPROD WHERE IDNODE = PRODUZIONE.IDNODE),''),
	GERARC_KEY = COALESCE((SELECT GERARC_KEY FROM AlberoPROD WHERE IDNODE = PRODUZIONE.IDNODE),'')
WHERE IDPRG NOT IN (SELECT IDPRG FROM PRODUZIONE WHERE FLGCLOSED = 1)