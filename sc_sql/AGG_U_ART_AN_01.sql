INSERT INTO U_ART_AN
            (ART_CODSOC,
             ART_CODPRI,
			 ART_TIPOG,
			 ART_TIPON,
			 ART_CODCOLRET,
			 ART_FINI,
			 ART_SCELTA,
			 ART_TOLGRM)
SELECT DISTINCT ART_CODSOC,
				ART_CODPRI,
				'' AS ART_TIPOG,
				'' AS ART_TIPON,
				'' AS ART_CODCOLRET,
				'' AS ART_FINI,
				'' AS ART_SCELTA,
				0 AS ART_TOLGRA
FROM U_ART_PR
WHERE ART_CODPRI NOT IN (SELECT ART_CODPRI AS ARTICOL FROM U_ART_AN)

UPDATE U_ART_AN SET ART_TOLGRM = (SELECT TOP 1 ART_TOLGRA FROM U_ART_PR AS PR WHERE PR.ART_CODPRI = U_ART_AN.ART_CODPRI)
