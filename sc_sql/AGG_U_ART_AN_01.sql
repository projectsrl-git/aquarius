
ALTER TABLE [dbo].[U_ART_AN] ADD 
	ART_TOLGRM numeric(17,1) NULL 
go

ALTER TABLE [dbo].[U_ART_AN] WITH NOCHECK ADD 
	CONSTRAINT [DF_U_ART_AN_ART_TOLGRM] DEFAULT (0)  FOR [ART_TOLGRM]
