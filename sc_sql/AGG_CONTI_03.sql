ALTER TABLE [dbo].[CONTI] ADD 
	 CON_CODRAG VARCHAR(8) COLLATE Latin1_General_CI_AS NULL,
	 CON_DESRAG VARCHAR(50) COLLATE Latin1_General_CI_AS NULL,
	 CON_CODCON VARCHAR(8) COLLATE Latin1_General_CI_AS NULL,
	 CON_DESCON VARCHAR(50) COLLATE Latin1_General_CI_AS NULL
go
-- forzature di default (vedi script standard per valorizzare campi diversi da varchar)
ALTER TABLE [dbo].[CONTI] WITH NOCHECK ADD 
	CONSTRAINT [DF_CONTI_CON_CODRAG] DEFAULT ('') FOR [CON_CODRAG],
	CONSTRAINT [DF_CONTI_CON_DESRAG] DEFAULT ('') FOR [CON_DESRAG],
	CONSTRAINT [DF_CONTI_CON_CODCON] DEFAULT ('') FOR [CON_CODCON],
	CONSTRAINT [DF_CONTI_CON_DESCON] DEFAULT ('') FOR [CON_DESCON]
go

UPDATE CONTI SET CON_CODRAG 	= SPACE(8)
UPDATE CONTI SET CON_DESRAG 	= SPACE(50)
UPDATE CONTI SET CON_CODCON 	= SPACE(8)
UPDATE CONTI SET CON_DESCON     = SPACE(50)