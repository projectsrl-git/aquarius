
-- VISTA ACQUISTI_BLACK_LIST

IF  EXISTS (SELECT * FROM sys.views WHERE object_id = OBJECT_ID(N'[dbo].[ACQUISTI_BLACK_LIST]'))
	DROP VIEW [dbo].[ACQUISTI_BLACK_LIST]
GO

CREATE VIEW [dbo].[ACQUISTI_BLACK_LIST]  
AS
	SELECT COALESCE(FORN.FOR_NAZION,'') AS NAZIONE, COALESCE(STA.DESCRI,'') AS DES_NAZIONE, COALESCE(NAZ_UNI.BLACK,' ') AS BLACK, IVF.* FROM U_IVF_CL AS IVF
	LEFT OUTER JOIN U_FOR_AN AS FORN ON FORN.FOR_CODCLI = IVF.IVF_CODCLI
	LEFT OUTER JOIN PARA AS STA ON STA.CODICE = 'STA'+FORN.FOR_NAZION
	LEFT OUTER JOIN NAZIONI_UNICO AS NAZ_UNI ON NAZ_UNI.CODICE = STA.DTIPOMOV
	WHERE NAZ_UNI.BLACK = 'X'
GO
