
SELECT
	(SELECT CODICEALIQUOTA FROM EC_ELINS.DBO.MovimentiIVA WHERE IDregistrazione = MC.MOV_NREGIS AND RIGAREGISTRAZIONE = MC.MOV_SEQUEN) AS CODICEALIQUOTA
	,(SELECT IMPOSTA FROM EC_ELINS.DBO.MovimentiIVA WHERE IDregistrazione = MC.MOV_NREGIS AND RIGAREGISTRAZIONE = MC.MOV_SEQUEN) AS IMPOSTA
	,MC.MOV_IVA
	,MC.MOV_IMPOST
	,MC.*
FROM MOV_CONT AS MC
WHERE
	MC.MOV_DTREG >= '2023/04/01' AND MC.MOV_DTREG <= '2023/05/31' AND RTRIM(SUBSTRING(MC.MOV_NOTE,1,100)) = '### PN IMPORTATA ###'
	AND MC.MOV_TOP IN (SELECT SUBSTRING(CODICE,4,10) FROM PARA WHERE CODICE LIKE 'TOP%' AND SUBSTRING(LIBERA,2,1) = 'S')
	AND MC.MOV_CONTO NOT IN (SELECT CON_CONTO FROM CONTI WHERE CON_ANNO = '2023' AND CON_TIPOCO IN ('C','F'))
	AND MC.MOV_CONTO = '3.9.4.       '
ORDER BY MC.MOV_DTREG,MC.MOV_NREGIS

