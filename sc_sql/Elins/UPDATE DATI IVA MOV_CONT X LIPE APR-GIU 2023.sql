	MC.MOV_DTREG >= '2023/04/01' AND MC.MOV_DTREG <= '2023/05/31' AND RTRIM(SUBSTRING(MC.MOV_NOTE,1,100)) = '### PN IMPORTATA ###'
	AND MC.MOV_TOP IN (SELECT SUBSTRING(CODICE,4,10) FROM PARA WHERE CODICE LIKE 'TOP%' AND SUBSTRING(LIBERA,2,1) = 'S')
