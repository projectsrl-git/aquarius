
SELECT MOV_NOTE,* FROM INDICE WHERE MOV_DTREG < '2023/05/29' AND RTRIM(SUBSTRING(MOV_NOTE,1,100)) <> '### PN IMPORTATA ###' ORDER BY MOV_DTREG,MOV_NUMPRO

SELECT MOV_NOTE,* FROM MOV_CONT WHERE MOV_DTREG < '2023/05/29' AND RTRIM(SUBSTRING(MOV_NOTE,1,100)) <> '### PN IMPORTATA ###' ORDER BY MOV_DTREG,MOV_NUMPRO

SELECT MOV_NUMPRO FROM MOV_CONT WHERE MOV_DTREG < '2023/05/29' AND RTRIM(SUBSTRING(MOV_NOTE,1,100)) <> '### PN IMPORTATA ###' GROUP BY MOV_NUMPRO ORDER BY MOV_NUMPRO

SELECT IND.MOV_DTREG,IND.MOV_NREGIS,IND.MOV_DTDOC,IND.MOV_NDOC,IND.MOV_TOP,PARA.DESCRI FROM INDICE AS IND
LEFT OUTER JOIN PARA ON PARA.CODICE = 'TOP'+MOV_TOP
WHERE IND.MOV_DTREG < '2023/05/29' AND RTRIM(SUBSTRING(IND.MOV_NOTE,1,100)) <> '### PN IMPORTATA ###' ORDER BY IND.MOV_DTREG,IND.MOV_NUMPRO