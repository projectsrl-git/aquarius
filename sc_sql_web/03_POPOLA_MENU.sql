FROM MENU 
LEFT OUTER JOIN UTENTI 
ON MENU.RUOLI LIKE '%'+UTENTI.RUOLO+'%' 
WHERE UTENTI.RUOLO = 'A' 
AND MENU.FLAG = 'S' 
AND LEN(MENU.ORDINE)=2
ORDER BY MENU.ORDINE


